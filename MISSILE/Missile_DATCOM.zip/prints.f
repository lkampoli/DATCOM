      SUBROUTINE PRINTS(IM)
C
C***  SUBROUTINE TO PRINT THE AERODYNAMIC DATA FROM THE IOM
C***  (STATIC DATA)
C
      COMMON /CONST/  PI,RAD,UNUSED,KAND
      COMMON /FLC/    NALPHA,ALPHA(20),BETA,PHI,NMACH,MACH(20),
     1                ALT(20),REN(20),VINF(20),TINF(20),PINF(20)
C
C  EFFECTIVE FULL FIN DEFLECTIONS
C
      COMMON /FFINDL/DSYN1(8),DSYN2(8),DSYN3(8),DSYN4(8),CLD(4)
C
      REAL NALPHA,NMACH,MACH
C
      COMMON /THERY/  LSOSE,PRESUR,LHYBRD,LHYPER
      LOGICAL LSOSE,PRESUR,LHYBRD,LHYPER
      COMMON /SBODY/ CN(20),CM(20),CA(20),CY(20),CLN(20),CLL(20),
     1               CNA(20),CMA(20),CYB(20),CNB(20),CLB(20)
C
      DIMENSION CBOD(220)
      EQUIVALENCE (CBOD(1),CN(1))
C
      COMMON /FSET1/ F1IN(399)
      COMMON /FSET2/ F2IN(399)
      COMMON /FSET3/ F3IN(399)
      COMMON /FSET4/ F4IN(399)
      COMMON /SFIN1/ CFIN1(220)
      COMMON /SFIN2/ CFIN2(220)
      COMMON /SFIN3/ CFIN3(220)
      COMMON /SFIN4/ CFIN4(220)
      COMMON /SB1/   CB1(220)
      COMMON /SB12/  CB12(220)
      COMMON /SB123/ CB123(220)
      COMMON /SB1234/CB1234(220)
      COMMON /INCID/ DELTA1(8),DELTA2(8),DELTA3(8),DELTA4(8),XHINGE(4),
     1               SKEW(4)
      COMMON /LOGIC/ LDMPCS,LDAMP,LBUILD,LNACA,LDERDG,
     1               LDERRD,LPART,LNAME,LPLOT,
     2               LFLT,LREFQ,LAXIS,LFIN1,LFIN2,LFIN3,LFIN4,
     3               LDEFL,LTRIM,LDIMIN,LDIMFT,LDIMCM,LDIMM,LELLB,
     4               LINLET,LEXPR,LICRMT,LSPIN,LARBOD
      LOGICAL        LDMPCS,LDAMP,LBUILD,LNACA,LDERDG,
     1               LDERRD,LPART,LNAME,LPLOT,
     2               LFLT,LREFQ,LAXIS,LFIN1,LFIN2,LFIN3,LFIN4,
     3               LDEFL,LTRIM,LDIMIN,LDIMFT,LDIMCM,LDIMM,LELLB,
     4               LINLET,LEXPR,LICRMT,LSPIN,LARBOD
C
      LOGICAL LBODY,LPANEL,LSYN1,LSYN2,LSYN3,LSYN4
C
      DATA IF1,IF2,IF3,IF4 / 1, 2, 3, 4 /
      DATA IC1,IC2,IC3,IC4,IC5,IC6,IC7,IC8,IC9 /1,2,3,4,5,6,7,8,9/
C
      NFIN1=F1IN(112)+.5
      NFIN2=F2IN(112)+.5
      NFIN3=F3IN(112)+.5
      NFIN4=F4IN(112)+.5
      DO 1000 I=1,8
         IF(ABS(DELTA1(I)) .LE. UNUSED)DELTA1(I)=0.
         IF(ABS(DELTA2(I)) .LE. UNUSED)DELTA2(I)=0.
         IF(ABS(DELTA3(I)) .LE. UNUSED)DELTA3(I)=0.
         IF(ABS(DELTA4(I)) .LE. UNUSED)DELTA4(I)=0.
C
         IF(ABS(DSYN1(I)) .LE. UNUSED)DSYN1(I)=0.
         IF(ABS(DSYN2(I)) .LE. UNUSED)DSYN2(I)=0.
         IF(ABS(DSYN3(I)) .LE. UNUSED)DSYN3(I)=0.
         IF(ABS(DSYN4(I)) .LE. UNUSED)DSYN4(I)=0.
 1000 CONTINUE
      LSYN1=.FALSE.
      LSYN2=.FALSE.
      LSYN3=.FALSE.
      LSYN4=.FALSE.
C
C  CHECK STATUS OF EFFECTIVE FULL FIN DEFLECTIONS
C
      DO 5 K=1,8
        IF(DSYN1(K).NE.DELTA1(K) .AND. ABS(DELTA1(K)).GT.UNUSED)
     1        LSYN1=.TRUE.
        IF(DSYN2(K).NE.DELTA2(K) .AND. ABS(DELTA2(K)).GT.UNUSED)
     2        LSYN2=.TRUE.
        IF(DSYN3(K).NE.DELTA3(K) .AND. ABS(DELTA3(K)).GT.UNUSED)
     3        LSYN3=.TRUE.
        IF(DSYN4(K).NE.DELTA4(K) .AND. ABS(DELTA4(K)).GT.UNUSED)
     4        LSYN4=.TRUE.
 5     CONTINUE
      IF(.NOT.LSYN1 .OR. .NOT.LSYN2 .OR. .NOT.LSYN3 .OR. .NOT.LSYN4)
     1 LPANEL=.TRUE.
C
C***  DETERMINE LAST CONFIGURATION TO OUTPUT
C
      LBODY=LAXIS .OR. LELLB .OR. LARBOD
      IF(LBODY)ILAST=1
      IF(LFIN1)ILAST=2
      IF(LFIN2)ILAST=3
      IF(LFIN3)ILAST=4
      IF(LFIN4)ILAST=5
      IF(LBODY.AND.LFIN1)ILAST=6
      IF(LBODY.AND.LFIN1.AND.LFIN2)ILAST=7
      IF(LBODY.AND.LFIN1.AND.LFIN2.AND.LFIN3)ILAST=8
      IF(LBODY.AND.LFIN1.AND.LFIN2.AND.LFIN3.AND.LFIN4)ILAST=9
C
C***  DETERMINE IF BUILD CASE
C
      IF(LBUILD)GO TO 1010
      IF(ILAST .LT. 1 .OR. ILAST .GT. 9)GO TO 1100
C
C***  NOT BUILD, JUMP TO LAST CONFIGURATION OUTPUTS
C
      GO TO(1010,1020,1030,1040,1050,1060,1070,1080,1090),ILAST
C
 1010 IF(.NOT. LBODY)GO TO 1020
C
C***  BODY ALONE
C
      CALL PRIIOM(IC1,IM,CBOD(1),CBOD(21),CBOD(41),CBOD(61),
     1 CBOD(81),CBOD(101),CBOD(121),CBOD(141),CBOD(161),
     2 CBOD(181),CBOD(201))
C
      IF(MACH(IM).GT.1.2 .AND. .NOT. LHYBRD .AND. .NOT. LHYPER)
     1 WRITE(6,1200)
      IF(MACH(IM).GT.1.2 .AND. LHYBRD .AND. .NOT. LHYPER)WRITE(6,1210)
      IF(MACH(IM).GT.1.2 .AND. LHYPER)WRITE(6,1220)
      IF(LINLET)WRITE(6,1290)
      IF(LEXPR)WRITE(6,1300)
C
 1020 IF(.NOT.LFIN1)GO TO 1030
C
C***  FIN 1 ALONE
C
      CALL PRIIOM(IC2,IM,CFIN1(1),CFIN1(21),CFIN1(41),CFIN1(61),
     1 CFIN1(81),CFIN1(101),CFIN1(121),CFIN1(141),CFIN1(161),
     2 CFIN1(181),CFIN1(201))
C
 1030 IF(.NOT. LFIN2)GO TO 1040
C
C***  FIN 2 ALONE
C
      CALL PRIIOM(IC3,IM,CFIN2(1),CFIN2(21),CFIN2(41),CFIN2(61),
     1 CFIN2(81),CFIN2(101),CFIN2(121),CFIN2(141),CFIN2(161),
     2 CFIN2(181),CFIN2(201))
C
 1040 IF(.NOT. LFIN3)GO TO 1050
C
C***  FIN 3 ALONE
C
      CALL PRIIOM(IC4,IM,CFIN3(1),CFIN3(21),CFIN3(41),CFIN3(61),
     1 CFIN3(81),CFIN3(101),CFIN3(121),CFIN3(141),CFIN3(161),
     2 CFIN3(181),CFIN3(201))
C
 1050 IF(.NOT. LFIN4)GO TO 1060
C
C***  FIN 4 ALONE
C
      CALL PRIIOM(IC5,IM,CFIN4(1),CFIN4(21),CFIN4(41),CFIN4(61),
     1 CFIN4(81),CFIN4(101),CFIN4(121),CFIN4(141),CFIN4(161),
     2 CFIN4(181),CFIN4(201))
C
 1060 IF(.NOT. (LBODY.AND.LFIN1))GO TO 1070
C
C***  BODY-FIN 1
C
      CALL PRIIOM(IC6,IM,CB1(1),CB1(21),CB1(41),CB1(61),
     1 CB1(81),CB1(101),CB1(121),CB1(141),CB1(161),
     2 CB1(181),CB1(201))
C
      IF(.NOT.LSYN1) WRITE(6,1230)
      IF(.NOT.LSYN1) WRITE(6,1240)
      IF(.NOT.LSYN1) WRITE(6,1260) IF1,(DELTA1(IN),IN=1,NFIN1)
C
      IF(LSYN1) WRITE(6,1330)
      IF(LSYN1) WRITE(6,1240)
      IF(LSYN1) WRITE(6,1260) IF1,(DELTA1(IN),IN=1,NFIN1)
C
      IF(LSYN1)WRITE(6,1430)
      IF(LSYN1)WRITE(6,1240)
      IF(LSYN1) WRITE(6,1260) IF1,(DSYN1(IN),IN=1,NFIN1)
C
      IF(MACH(IM).GT.1.2 .AND. .NOT. LHYBRD .AND. .NOT. LHYPER)
     1 WRITE(6,1200)
      IF(MACH(IM).GT.1.2 .AND. LHYBRD .AND. .NOT. LHYPER)WRITE(6,1210)
      IF(MACH(IM).GT.1.2 .AND. LHYPER)WRITE(6,1220)
      IF(LINLET)WRITE(6,1290)
      IF(LEXPR)WRITE(6,1300)
C
 1070 IF(.NOT. (LBODY.AND.LFIN1.AND.LFIN2))GO TO 1080
C
C *** BODY-FIN1-FIN2
C
      CALL PRIIOM(IC7,IM,CB12(1),CB12(21),CB12(41),CB12(61),
     1 CB12(81),CB12(101),CB12(121),CB12(141),CB12(161),
     2 CB12(181),CB12(201))
C
      IF(.NOT.LSYN1 .OR. .NOT.LSYN2) WRITE(6,1230)
      IF(.NOT.LSYN1 .OR. .NOT.LSYN2) WRITE(6,1240)
      IF(.NOT.LSYN1) WRITE(6,1260) IF1,(DELTA1(IN),IN=1,NFIN1)
      IF(.NOT.LSYN2) WRITE(6,1260) IF2,(DELTA2(IN),IN=1,NFIN2)
C
      IF(LSYN1 .OR. LSYN2)WRITE(6,1330)
      IF(LSYN1 .OR. LSYN2)WRITE(6,1240)
      IF(LSYN1) WRITE(6,1260) IF1,(DELTA1(IN),IN=1,NFIN1)
      IF(LSYN2) WRITE(6,1260) IF2,(DELTA2(IN),IN=1,NFIN2)
C
      IF(LSYN1 .OR. LSYN2)WRITE(6,1430)
      IF(LSYN1 .OR. LSYN2)WRITE(6,1240)
      IF(LSYN1) WRITE(6,1260) IF1,(DSYN1(IN),IN=1,NFIN1)
      IF(LSYN2) WRITE(6,1260) IF2,(DSYN2(IN),IN=1,NFIN2)
C
      IF(MACH(IM).GT.1.2 .AND. .NOT. LHYBRD .AND. .NOT. LHYPER)
     1 WRITE(6,1200)
      IF(MACH(IM).GT.1.2 .AND. LHYBRD .AND. .NOT. LHYPER)WRITE(6,1210)
      IF(MACH(IM).GT.1.2 .AND. LHYPER)WRITE(6,1220)
      IF(LINLET)WRITE(6,1290)
      IF(LEXPR)WRITE(6,1300)
C
 1080 IF(.NOT. (LBODY.AND.LFIN1.AND.LFIN2.AND.LFIN3))GO TO 1090
C
C***  BODY-FIN 1-FIN 2-FIN 3
C
      CALL PRIIOM(IC8,IM,CB123(1),CB123(21),CB123(41),CB123(61),
     1 CB123(81),CB123(101),CB123(121),CB123(141),CB123(161),
     2 CB123(181),CB123(201))
C
      IF(.NOT.LSYN1 .OR. .NOT.LSYN2 .OR. .NOT.LSYN3) WRITE(6,1230)
      IF(.NOT.LSYN1 .OR. .NOT.LSYN2 .OR. .NOT.LSYN3) WRITE(6,1240)
      IF(.NOT.LSYN1) WRITE(6,1260) IF1,(DELTA1(IN),IN=1,NFIN1)
      IF(.NOT.LSYN2) WRITE(6,1260) IF2,(DELTA2(IN),IN=1,NFIN2)
      IF(.NOT.LSYN3) WRITE(6,1260) IF3,(DELTA3(IN),IN=1,NFIN3)
C
      IF(LSYN1 .OR. LSYN2 .OR. LSYN3)WRITE(6,1330)
      IF(LSYN1 .OR. LSYN2 .OR. LSYN3)WRITE(6,1240)
      IF(LSYN1) WRITE(6,1260) IF1,(DELTA1(IN),IN=1,NFIN1)
      IF(LSYN2) WRITE(6,1260) IF2,(DELTA2(IN),IN=1,NFIN2)
      IF(LSYN3) WRITE(6,1260) IF3,(DELTA3(IN),IN=1,NFIN3)
C
      IF(LSYN1 .OR. LSYN2 .OR. LSYN3)WRITE(6,1430)
      IF(LSYN1 .OR. LSYN2 .OR. LSYN3)WRITE(6,1240)
      IF(LSYN1) WRITE(6,1260) IF1,(DSYN1(IN),IN=1,NFIN1)
      IF(LSYN2) WRITE(6,1260) IF2,(DSYN2(IN),IN=1,NFIN2)
      IF(LSYN3) WRITE(6,1260) IF3,(DSYN3(IN),IN=1,NFIN3)
C
      IF(MACH(IM).GT.1.2 .AND. .NOT. LHYBRD .AND. .NOT. LHYPER)
     1 WRITE(6,1200)
      IF(MACH(IM).GT.1.2 .AND. LHYBRD .AND. .NOT. LHYPER)WRITE(6,1210)
      IF(MACH(IM).GT.1.2 .AND. LHYPER)WRITE(6,1220)
      IF(LINLET)WRITE(6,1290)
      IF(LEXPR)WRITE(6,1300)
C
 1090 IF(.NOT. (LBODY.AND.LFIN1.AND.LFIN2.AND.LFIN3.AND.LFIN4))
     1   GO TO 1100
C
C***  BODY-FIN 1-FIN 2-FIN 3-FIN 4
C
      CALL PRIIOM(IC9,IM,CB1234(1),CB1234(21),CB1234(41),CB1234(61),
     1 CB1234(81),CB1234(101),CB1234(121),CB1234(141),CB1234(161),
     2 CB1234(181),CB1234(201))
C
      IF(.NOT.LSYN1 .OR. .NOT.LSYN2 .OR. .NOT.LSYN3 .OR.
     1   .NOT.LSYN4) WRITE(6,1230)
      IF(.NOT.LSYN1 .OR. .NOT.LSYN2 .OR. .NOT.LSYN3 .OR.
     1   .NOT.LSYN4) WRITE(6,1240)
      IF(.NOT.LSYN1) WRITE(6,1260) IF1,(DELTA1(IN),IN=1,NFIN1)
      IF(.NOT.LSYN2) WRITE(6,1260) IF2,(DELTA2(IN),IN=1,NFIN2)
      IF(.NOT.LSYN3) WRITE(6,1260) IF3,(DELTA3(IN),IN=1,NFIN3)
      IF(.NOT.LSYN4) WRITE(6,1260) IF4,(DELTA4(IN),IN=1,NFIN4)
C
      IF(LSYN1 .OR. LSYN2 .OR. LSYN3 .OR. LSYN4)WRITE(6,1330)
      IF(LSYN1 .OR. LSYN2 .OR. LSYN3 .OR. LSYN4)WRITE(6,1240)
      IF(LSYN1) WRITE(6,1260) IF1,(DELTA1(IN),IN=1,NFIN1)
      IF(LSYN2) WRITE(6,1260) IF2,(DELTA2(IN),IN=1,NFIN2)
      IF(LSYN3) WRITE(6,1260) IF3,(DELTA3(IN),IN=1,NFIN3)
      IF(LSYN4) WRITE(6,1260) IF4,(DELTA4(IN),IN=1,NFIN4)
C
      IF(LSYN1 .OR. LSYN2 .OR. LSYN3 .OR. LSYN4)WRITE(6,1430)
      IF(LSYN1 .OR. LSYN2 .OR. LSYN3 .OR. LSYN4)WRITE(6,1240)
      IF(LSYN1) WRITE(6,1260) IF1,(DSYN1(IN),IN=1,NFIN1)
      IF(LSYN2) WRITE(6,1260) IF2,(DSYN2(IN),IN=1,NFIN2)
      IF(LSYN3) WRITE(6,1260) IF3,(DSYN3(IN),IN=1,NFIN3)
      IF(LSYN4) WRITE(6,1260) IF4,(DSYN4(IN),IN=1,NFIN4)
C
      IF(MACH(IM).GT.1.2 .AND. .NOT. LHYBRD .AND. .NOT. LHYPER)
     1 WRITE(6,1200)
      IF(MACH(IM).GT.1.2 .AND. LHYBRD .AND. .NOT. LHYPER)WRITE(6,1210)
      IF(MACH(IM).GT.1.2 .AND. LHYPER)WRITE(6,1220)
      IF(LINLET)WRITE(6,1290)
      IF(LEXPR)WRITE(6,1300)
C
 1100 CONTINUE
C
 1200 FORMAT(/,4X,'BODY ALONE LINEAR DATA GENERATED FROM ',
     1          'SECOND ORDER SHOCK EXPANSION METHOD',/)
 1210 FORMAT(/,4X,'BODY ALONE LINEAR DATA GENERATED FROM ',
     1          'VAN DYKE HYBRID THEORY',/)
 1220 FORMAT(/,4X,'BODY ALONE LINEAR DATA GENERATED FROM ',
     1          'MODIFIED NEWTONIAN FLOW THEORY',/)
 1230 FORMAT(/,4X,'PANEL DEFLECTION ANGLES (DEGREES)')
 1240 FORMAT(4X,'SET',4X,'FIN 1',3X,'FIN 2',3X,'FIN 3',3X,'FIN 4',
     1       3X,'FIN 5',3X,'FIN 6',3X,'FIN 7',3X,'FIN 8')
 1250 FORMAT(4X,'FIN 5',6X,'FIN 6',6X,'FIN 7',6X,'FIN 8')
 1260 FORMAT(5X,I2,8F8.2)
 1270 FORMAT(5X,F7.2,4X,F7.2,4X,F7.2,4X,F7.2)
 1330 FORMAT(/,4X,'FLAP DEFLECTION ANGLES (DEGREES)')
 1430 FORMAT(4X,'EQUIVALENT PANEL DEFLECTION ANGLES (DEGREES)')
 1290 FORMAT(4X,'AERODYNAMICS INCLUDE THE EFFECT OF AN AIRBREATHING ',
     1          'INLET')
 1300 FORMAT(4X,'THIS OUTPUT MAY INCLUDE THE EFFECT OF ',
     1          'EXPERIMENTAL DATA INPUTS')
      RETURN
      END
