      SUBROUTINE SUMPOT(ORDER,BETA,I,C,ZE0,ZE0X,ZE0R,ZE0XX,
     1    ZE0XR,ZE0RR,NN1,NN2,NN3)
C
C  ROUTINE ADAPTED FROM NSWC AEROPREDICTION CODE
C  ORIGINAL ROUTINE WRITTEN BY F. MOORE, NSWC
C  MODIFIED BY J. JENKINS, WL/FIGC
C
      COMMON /VDARY/ XB(220),RB(220),RBP(220),DMM1(220),C1(220),
     1  RB1(220),RBP1(220),B(220),PSI(220),DMM2(220),PSIR(220),
     2  PHIX(220)
      DIMENSION C(220),ZE0X(220)
      INTEGER ORDER
      SUM1=0.
      SUM2=0.
      SUM3=0.
      SUM4=0.
      SUM5=0.
      SUM6=0.
      NTERM=3
      IF(ORDER .EQ. 1) NTERM=6
C
C  J=1 IS CONICAL SOLN. WHICH WILL BE ADDED IN BELOW.
C
      DO 1000 J=2,I
C
C ... SOLUTION OF J.A.S. EQUATION 26
C
         CALL JAS26(XB(I),BETA,RB(J-1),XB(J-1),XXI,RB(I),TAU,F1,F2,
     1        C(J),SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,-1.,NTERM)
 1000 CONTINUE
      JH=NN1+1
      IF(I.LE.NN1) GO TO 1010
      CALL DISC2(I,JH,BETA,SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,NTERM,ORDER)
      J=NN1+1
      IF(NN2 .EQ. 0)GO TO 1005
C
C ... SOLUTION OF J.A.S. EQUATION 26
C
      CALL JAS26(XB(I),BETA,RB(J-1),XB(J-1),XXI,RB(I),TAU,F1,F2,
     1        C(J),SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,+1.,NTERM)
      JH=NN2+1
      IF(I.LE.NN2) GO TO 1010
      CALL DISC2(I,JH,BETA,SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,NTERM,ORDER)
      J=NN2+1
C
C ... SOLUTION OF J.A.S. EQUATION 26
C
 1005 CALL JAS26(XB(I),BETA,RB(J-1),XB(J-1),XXI,RB(I),TAU,F1,F2,
     1        C(J),SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,+1.,NTERM)
      JH=NN3+1
      IF(I.LE.NN3) GO TO 1010
      CALL DISC2(I,JH,BETA,SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,NTERM,ORDER)
      J=NN3+1
C
C ... SOLUTION OF J.A.S. EQUATION 26
C
      CALL JAS26(XB(I),BETA,RB(J-1),XB(J-1),XXI,RB(I),TAU,F1,F2,
     1        C(J),SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,+1.,NTERM)
 1010 XIMX1=XB(I)-XB(1) +BETA*RB(1)
C
C ... SOLUTION OF J.A.S. EQUATION 25
C
      TAU=BETA*RB(I)/XIMX1
      IF(TAU.GE.1.) TAU=0.9999
      F1=ARSECH(TAU)
      F2=SQRT(1.-TAU**2)
      ZE0X(I)=SUM2-F1*C(1)
      ZE0R=SUM3+BETA*F2/TAU*C(1)
      IF(ORDER .NE. 1) GO TO 1020
      ZE0=SUM1+XIMX1*(F2-F1)*C(1)
      ZE0XX=-1./(XIMX1*F2)*C(1)+SUM4
      ZE0XR=BETA/(XIMX1*TAU*F2)*C(1)+SUM5
      ZE0RR=-BETA**2/(XIMX1*TAU**2*F2)*C(1)+SUM6
 1020 CONTINUE
      RETURN
      END
