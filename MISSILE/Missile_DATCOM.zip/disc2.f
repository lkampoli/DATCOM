     51.34875,1.35439,1.35991,1.36531,1.37059,1.37575,1.38082,1.38577,
     61.39063,1.39539,1.40005,1.40463,1.40911,1.41351,1.41783,1.42207,
     71.42623,1.43032,1.43433,1.43827,1.44214,1.44594,1.44968,1.45336,
     81.45697,1.46053,1.46402,1.46746,1.47085,1.47417,1.47745,1.48068,
     91.48385,1.48698,1.49006,1.49309,1.49607,1.49902,1.50192,1.50477,
     A1.50759,1.51036,1.51310,1.51579,1.51845,1.52107,1.52366,1.52621,
     B1.52872,1.53121,1.53365,1.53607,1.53845,1.54081,1.54313,1.54542,
     C1.54769,1.54992,1.55213,1.55430,1.55646,1.55858,1.56068,1.56275,
     D1.56480,1.56682,1.56882,1.570796/
C
      DO 800 II=1,100
      T(II)=TT(II)
      AK(II)=AKT(II)
      AE(II)=AET(II)
  800 CONTINUE
C
      XI=XB(JH)-BETA*RB(JH)
      TAU=BETA*RB(I)/(XB(I)-XI)
      IF(TAU.GE.1.) TAU=0.9999
C
C ... LOOK-UP COMPLETE ELLIPTIC INTEGRAL OF THE FIRST KIND
C
      CALL INTERP(T,AK,TAU,AKX,100,3)
C
C ... LOOK-UP COMPLETE ELLIPTIC INTEGRAL OF THE SECOND KIND
C
      CALL INTERP(T,AE,TAU,AEX,100,3)
      A=SQRT(XB(I)-XI)
      B=SQRT(1.+TAU)
      D=2.**1.5/PI
      E=ABS(RBP(JH)-RBP(JH-1))
      IF(ORDER .GT. 1) GO TO 1030
C
C ... FIRST ORDER SOLUTION
C       CURVATURE SOLUTION
C
        A1=C(JH)*SQRT(BETA*RB(JH))
        SUM1=SUM1-A1*A**1.5*4./9.*D*B*((3.+TAU)*AKX-4.*AEX)
        SUM2=SUM2-A1*A*2.*D*B*(AKX-AEX)
        SUM3=SUM3+A1*BETA*A*2./3.*D*B*(AEX/TAU-AKX)
        IF(NTERM .GT. 0 .AND. NTERM .LT. 6) GO TO 900
          SUM4=SUM4-A1/A*D*AKX/B
          SUM5=SUM5+A1*BETA/A*D/B*((1.+TAU)/TAU*AEX-AKX)
          SUM6=SUM6-A1*BETA**2/A*D/B*(2.*(1.+TAU)/TAU**2*AEX-(2.-TAU)/
     1    TAU*AKX)/3.
  900   CONTINUE
        IF(E.LT.0.0001) GO TO 1050
C
C      CORNER SOLUTION
C
          A2=A**2
          F=SQRT(RB(JH)/RB(I))/PI
          G=SQRT(2.*TAU)/B
          IF(TAU.GT.0.9999) GO TO 1000
            H=1./(1.-TAU)
 1000     H1=C(JH)
          SUM1 = SUM1-H1*4.*A2*F*B**2*G*(AKX-AEX)
          SUM2 = SUM2-H1*2.*F*G*AKX
          SUM3 = SUM3+H1*2.*BETA*F*G*(B**2*AEX/TAU-AKX)
          IF(NTERM .GT. 0 .AND. NTERM .LT. 6) GO TO 1050
            IF(TAU.LT.0.9999) GO TO 1010
              SUM4=SUM4+H1/(8.*BETA*RB(JH))
              SUM5=SUM5+3.*H1/(8.*RB(JH))
              SUM6=SUM6-7.*BETA*H1/(8.*RB(JH))
              GO TO 1050
 1010         SUM4 = SUM4+H1*F/A2*H*G*(AKX-AEX)
              SUM5 = SUM5+H1*BETA*F/A2*H*G*(AEX/TAU-AKX)
              SUM6 = SUM6-H1*BETA**2/A2*F*H*G*((2.-TAU**2)/TAU**2*AEX-
     1          (2.-TAU)/TAU*AKX)
              GO TO 1050
C
C     HERE IF 2-ND ORDER
C       CUVATURE SOLUTION
 1030   F=SQRT(RB(JH)/RB(I))/PI
        G=SQRT(2.*TAU)/B
        A2=A**2
        H1=C1(JH)*F*G
        SUM1=SUM1-H1*4.*A2*B**2*(AKX-AEX)
        SUM2=SUM2-H1*2.*AKX
        SUM3=SUM3+H1*2.*BETA*(B**2*AEX/TAU-AKX)
        IF(E.LT.0.0001) GO TO 1050
C
C           CORNER SOLUTION
C
          H2=C1(JH)*D*B*SQRT(BETA*RB(JH))
          SUM1=SUM1-H2*A**1.5*4./9.*((3.+TAU)*AKX-4.*AEX)
          SUM2=SUM2-H2*A*2.*(AKX-AEX)
          SUM3=SUM3+H2*BETA*A*2./3.*(AEX/TAU-AKX)
          SUM1=SUM1+C3*2.*G*F*AKX
          IF(TAU.LT.0.995) GO TO 1040
            SUM2=SUM2-C3/(8.*BETA*RB(JH))
            SUM3=SUM3-3.*C3/(8.*RB(JH))
            GO TO 1050
C
C        HERE IF TAU LESS THAN 0.995
C
 1040       H=1./(1.-TAU)
            SUM2=SUM2-C3/A2*F*H*G*(AKX-AEX)
            SUM3=SUM3-C3*BETA/A2*F*H*G*(AEX/TAU-AKX)
 1050 CONTINUE
      RETURN
      END
