     A .73000, .74000, .75000, .76000, .77000, .78000, .79000, .80000,
     B .81000, .82000, .83000, .84000, .85000, .86000, .87000, .88000,
     C .89000, .90000, .91000, .92000, .93000, .94000, .95000, .96000,
     D .97000, .98000, .99000,1.00000/
      DATA AKT /
     13.35902,3.02571,2.83492,2.70218,2.60107,2.51987,2.45234,2.39475,
     22.34473,2.30064,2.26132,2.22592,2.19380,2.16445,2.13748,2.11257,
     32.08946,2.06794,2.04782,2.02896,2.01123,1.99451,1.97871,1.96376,
     41.94957,1.93608,1.92324,1.91099,1.89929,1.88811,1.87740,1.86713,
     51.85727,1.84780,1.83870,1.82993,1.82148,1.81331,1.80547,1.79787,
     61.79053,1.78343,1.77655,1.76989,1.76344,1.75718,1.75111,1.74521,
     71.73948,1.73392,1.72851,1.72324,1.71812,1.71313,1.70827,1.70354,
     81.69892,1.69442,1.69003,1.68575,1.68157,1.67748,1.67350,1.66960,
     91.66579,1.66206,1.65842,1.65485,1.65137,1.64795,1.64461,1.64133,
     A1.63813,1.63499,1.63191,1.62889,1.62593,1.62303,1.62018,1.61739,
     B1.61465,1.61196,1.60932,1.60672,1.60418,1.60168,1.59922,1.59680,
     C1.59443,1.59210,1.58981,1.58755,1.58534,1.58316,1.58101,1.57890,
     D1.57683,1.57479,1.57278,1.570796/
      DATA AET /
     11.02836,1.04970,1.06835,1.08526,1.10085,1.11541,1.12909,1.14204,
     21.15433,1.16606,1.17727,1.18802,1.19835,1.20828,1.21786,1.22711,
     31.23604,1.24469,1.25307,1.26119,1.26907,1.27672,1.28416,1.29139,
