     4 .25000, .26000, .27000, .28000, .29000, .30000, .31000, .32000,
     5 .33000, .34000, .35000, .36000, .37000, .38000, .39000, .40000,
     6 .41000, .42000, .43000, .44000, .45000, .46000, .47000, .48000,
     7 .49000, .50000, .51000, .52000, .53000, .54000, .55000, .56000,
     8 .57000, .58000, .59000, .60000, .61000, .62000, .63000, .64000,
