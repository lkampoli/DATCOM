      SUBROUTINE DDCVRT
C      
C  WRITTEN BY W. BLAKE, WL/FIGC
C
C***    SUBROUTINE TO CONVERT DYNAMIC DERIVATIVES
C***    TO DEGREES IF REQUESTED
C
      COMMON /CONST/  PI,RAD,UNUSED,KAND
      COMMON /DBODY/  BD(406)
      COMMON /DB1/    D1(400)
      COMMON /DB12/   D12(400)
      COMMON /DB123/  D123(400)
      COMMON /DB1234/ D1234(400)
C      
      DO 1000 I=1,400
         BD(I)=BD(I)/RAD
         D1(I)=D1(I)/RAD
         D12(I)=D12(I)/RAD
         D123(I)=D123(I)/RAD
         D1234(I)=D1234(I)/RAD
 1000 CONTINUE
C
      DO 1050 I=1,6
         BD(400+I)=BD(400+I)/RAD
 1050 CONTINUE
C
      RETURN
      END
