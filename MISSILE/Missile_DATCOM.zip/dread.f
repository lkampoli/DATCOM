      SUBROUTINE DREAD(NSET,JUMP)
C
C***  SUBROUTINE TO DETERMINE NAMELIST TO BE READ FROM UNIT 2
C***  BUT THE DATA NOT USED ... ACTS AS A NAMELIST DELETE
C
      DIMENSION AA1(125),AA2(9),AA3(881),AA4(389),AA5(40),
     1          AA6(15),AA7(881),AA8(64),AA9(115)
C
      LOGICAL LDUM
C
      JJUMP=JUMP
C
 1000 IF(JJUMP .GT. 0 .AND. JJUMP .LT. 10)GO TO 1010
C
         JJUMP=JJUMP-100
         IF(JJUMP .LT. 0)GO TO 1100
         GO TO 1000
C
 1010 CONTINUE
C
      GO TO (1020,1030,1040,1050,1060,1070,1080,1090,1100,1095), JJUMP
C
 1020 CALL RFLT(LDUM, AA1)
      GO TO 1100
C
 1030 CALL RREFQ(LDUM, AA2)
      GO TO 1100
C
 1040 CALL RAXIS(LDUM, AA3)
      GO TO 1100
C
 1050 CALL RFIN1(LDUM, AA4)
      GO TO 1100
C
 1060 CALL RDEFL(LDUM, AA5)
      GO TO 1100
C
 1070 CALL RTRIM(LDUM, AA6)
      GO TO 1100
C
 1080 CALL RELLB(LDUM, AA7)
      GO TO 1100
C
 1090 CALL RINLET(LDUM, AA8)
      GO TO 1100
C
 1095 CALL RARBOD(LDUM,AA9)
C
 1100 CONTINUE
C
      RETURN
      END
