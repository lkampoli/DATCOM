      SUBROUTINE PRIF2
C
C***  SUBROUTINE TO PRINT THE RESULTS FOR FIN SET 2
C
      COMMON /FSET2/  FDATA(399)
C
      COMMON /GEOFS2/ GFIN(188)
C
      DIMENSION SSPAN(10),LMAXU(10),LFLATU(10),LMAXL(10),
     1          LFLATL(10),CHORD(10),THICKU(10),THICKL(10),
     2          SWEEP(10),STA(10),XLE(10),
     3          XCORD(50),MEAN(50),THICK(50),YUPPER(50),
     4          YLOWER(50),LER(10)
C
      REAL LER,LMAXU,LMAXL,LFLATU,LFLATL,NPANEL,MEAN
C
      EQUIVALENCE (FDATA(1),SECTYP),(FDATA(2),SSPAN(1)),
     1            (FDATA(12),LMAXU(1)),(FDATA(22),LFLATU(1)),
     2            (FDATA(32),LMAXL(1)),(FDATA(42),LFLATL(1)),
     3            (FDATA(52),CHORD(1)),(FDATA(62),THICKU(1)),
     4            (FDATA(72),THICKL(1)),(FDATA(82),SWEEP(1)),
     5            (FDATA(92),STA(1)),(FDATA(102),XLE(1)),
     6            (FDATA(112),NPANEL),(FDATA(113),XCORD(1)),
     7            (FDATA(163),MEAN(1)),(FDATA(213),THICK(1)),
     8            (FDATA(263),YUPPER(1)),(FDATA(313),YLOWER(1)),
     9            (FDATA(363),FINPHI),(FDATA(364),LER(1))
C
      DIMENSION SPLANF(10),CBAR(10),SWEPLE(10),SWEP25(10),
     1          SWEP50(10),SWEP75(10),SWEPTE(10),
     2          XMGCLE(10),XMGC25(10),
     3          XMGC50(10),XMGC75(10),XMGCTE(10),YMEANC(10),
     4          ASPCT(10),TAPER(10),TOVERC(10),XCENT(10)
C
      EQUIVALENCE (GFIN(1),SPLANF(1)),(GFIN(11),CBAR(1)),
     1            (GFIN(21),SWEPLE(1)),(GFIN(31),SWEP25(1)),
     2            (GFIN(41),SWEP50(1)),(GFIN(51),SWEP75(1)),
     3            (GFIN(61),SWEPTE(1)),(GFIN(71),XMGCLE(1)),
     4            (GFIN(81),XMGC25(1)),(GFIN(91),XMGC50(1)),
     5            (GFIN(101),XMGC75(1)),(GFIN(111),XMGCTE(1)),
     6            (GFIN(121),YMEANC(1)),(GFIN(131),ASPCT(1)),
     7            (GFIN(141),TAPER(1)),(GFIN(151),TOVERC(1)),
     8            (GFIN(161),XCENT(1)),(GFIN(171),FINAR),
     9            (GFIN(172),FINTPR),(GFIN(173),FINSPN),
     A            (GFIN(174),FINSP),(GFIN(175),FINWET),
     B            (GFIN(176),TCEFF),(GFIN(177),FINXGC),
     C            (GFIN(178),FINYGC),(GFIN(179),SWAVLE),
     D            (GFIN(180),SWAV25),(GFIN(181),SWAV50),
     E            (GFIN(182),SWAV75),(GFIN(183),SWAVTE),
     F            (GFIN(184),FINCBR),(GFIN(185),DELTAY),
     G            (GFIN(186),DELTAD),(GFIN(187),XCENF),(GFIN(188),XOVC)
C
      COMMON /CONST/ PI,RAD,UNUSED,KAND
C
      COMMON /LOGIC/ LDMPCS,LDAMP,LBUILD,LNACA,LDERDG,
     1               LDERRD,LPART,LNAME,LPLOT,
     2               LFLT,LREFQ,LAXIS,LFIN1,LFIN2,LFIN3,LFIN4,
     3               LDEFL,LTRIM,LDIMIN,LDIMFT,LDIMCM,LDIMM,LELLB,
     4               LINLET,LEXPR,LICRMT,LSPIN,LARBOD
      LOGICAL        LDMPCS,LDAMP,LBUILD,LNACA,LDERDG,
     1               LDERRD,LPART,LNAME,LPLOT,
     2               LFLT,LREFQ,LAXIS,LFIN1,LFIN2,LFIN3,LFIN4,
     3               LDEFL,LTRIM,LDIMIN,LDIMFT,LDIMCM,LDIMM,LELLB,
     4               LINLET,LEXPR,LICRMT,LSPIN,LARBOD
C
      CHARACTER*4 IFT,ICM,IM,IIN,IUNIT
C
      DATA IFT/'  FT'/
      DATA ICM/'  CM'/
      DATA IM /'   M'/
      DATA IIN/'  IN'/
C
      IUNIT=IFT
      IF(LDIMCM)IUNIT=ICM
      IF(LDIMM)IUNIT=IM
      IF(LDIMIN)IUNIT=IIN
C
      ISET=2
C
      WRITE(6,1030)ISET
      WRITE(6,1040)
      WRITE(6,1050)
      WRITE(6,1060)
      IF(LDIMFT) WRITE(6,1082) IUNIT
      IF(LDIMIN) WRITE(6,1084)
      IF(LDIMM)  WRITE(6,1086)
      IF(LDIMCM) WRITE(6,1088)
C
      NSEG=1
C
C***  FIND LAST SPAN STATION
C
      DO 1000 I=2,10
          IF(SSPAN(I) .EQ. UNUSED .OR. SSPAN(I) .EQ. 0.)GO TO 1010
          NSEG=I-1
 1000 CONTINUE
C
 1010 CONTINUE
C
C***  PRINT RESULTS FOR EACH FIN SEGMENT
C
      DO 1020 ISEG=1,NSEG
         WRITE(6,1090)ISEG,SPLANF(ISEG),ASPCT(ISEG),TAPER(ISEG),
     1   SWEPLE(ISEG),SWEPTE(ISEG),CBAR(ISEG),TOVERC(ISEG)
 1020 CONTINUE
C
      WRITE(6,1100)FINSP,FINAR,FINTPR,SWAVLE,SWAVTE,FINCBR,TCEFF
C
 1030 FORMAT(29X,'FIN SET NUMBER ',I2)
 1040 FORMAT(26X,'(DATA FOR ONE PANEL ONLY)',/)
 1050 FORMAT(4X,'SEGMENT',3X,'PLAN',8X,'ASPECT',4X,'TAPER',5X,
     1 'L.E.',5X,'T.E.',4X,'M.A.C.',4X,'T/C')
 1060 FORMAT(4X,'NUMBER',4X,'AREA',8X,'RATIO',5X,'RATIO',4X,'SWEEP',
     1 4X,'SWEEP',4X,'CHORD',4X,'RATIO')
 1082 FORMAT(4X,A4,6X,'FT**2',27X,'DEG',6X,'DEG',7X,'FT')
 1084 FORMAT(4X,A4,6X,'IN**2',27X,'DEG',6X,'DEG',7X,'IN')
 1086 FORMAT(4X,A4,6X,' M**2',27X,'DEG',6X,'DEG',7X,' M')
 1088 FORMAT(4X,A4,6X,'CM**2',27X,'DEG',6X,'DEG',7X,'CM')
 1090 FORMAT(4X,1X,I2,2X,F10.4,4X,6F9.3)
 1100 FORMAT(4X, 'TOTAL',F10.4,4X,6F9.3)
C
      RETURN
      END
