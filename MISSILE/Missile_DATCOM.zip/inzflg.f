      SUBROUTINE INZFLG(CASE,SAVE)
C
C***  INITIALIZE ALL PROGRAM FLAGS
C
C***  PROGRAM LOGIC FLAGS
C
      COMMON /LOGIC/  LFLAGS(28)
      COMMON /DFLAGS/ DFLGG(12)
      COMMON /DUMPF/  LDUMPF(37)
C
      LOGICAL DFLGG,LFLAGS,SAVE,LDUMPF
C
      COMMON /CASEID/ IDCASE(74),KOUNT,NAMSV(100),KASE,NOEXTR,NOLAT,
     1                IR,IPAGE
      LOGICAL NOEXTR,NOLAT
C
      COMMON /DESIG/  NNACA(80,4)
C
      COMMON /INC/ COREC(150)
C
C***  PROGRAM CONSTANTS
C
      COMMON /CONST/  PI,RAD,UNUSED,KAND
C
      COMMON /THERY/  LSOSE,PRESUR,LHYBRD,LHYPER
C
      LOGICAL LSOSE,PRESUR,LHYBRD,LHYPER
C
      INTEGER CASE
      CHARACTER*4 IBLANK,IDCASE
C
      DATA IBLANK /'    '/
C
      DO 1000 I=1,28
C
C***  SKIP INITIALIZATION OF FLAGS 5,6,19,20,21,22 IF NOT FIRST CASE
C***  THESE ARE THE DERIVATIVE AND DIMENSION FLAGS, RESPECTIVELY
C***  ALSO, DO NOT RE-INITIALIZE FLAG 9 (PLOT CONTROL)
C
         IF(CASE .NE. 1 .AND. (I .EQ. 5 .OR. I .EQ. 6 .OR. I .EQ. 9
     1   .OR. I .EQ. 19 .OR. I .EQ. 20 .OR. I .EQ. 21 .OR. I .EQ. 22))
     2      GO TO 1000
C
C***  DO NOT RE-INITIALIZE FLAG 4 (NACA INPUTS) IF SAVE CASE
C
         IF(CASE .NE. 1 .AND. SAVE .AND. I .EQ. 4)GO TO 1000
C
C***  DO NOT RE-INITIALIZE FLAG 26 (INCREMENT CARD) UNLESS FIRST CASE
C
         IF(CASE .NE. 1 .AND. I .EQ. 26)GO TO 1000
C
         LFLAGS(I)=.FALSE.
C
 1000 CONTINUE
C
C
C***  FLAG 20 IS FOR DIMENSIONS OF FEET, DEFAULT TO TRUE
C
      IF(.NOT.(LFLAGS(19).OR.LFLAGS(21).OR.LFLAGS(22)))LFLAGS(20)=.TRUE.
C
      DO 1010 I=1,74
         IDCASE(I)=IBLANK
 1010 CONTINUE
      NOEXTR=.TRUE.
      NOLAT=.FALSE.
C
      IF(SAVE .AND. CASE .NE. 1)GO TO 1040
C
C***  INITIALIZE NACA CARD (ALSO INITIALIZED IN INZFIN)
C
      DO 1030 I=1,80
         DO 1020 J=1,4
            NNACA(I,J)=UNUSED
 1020    CONTINUE
 1030 CONTINUE
C
 1040 CONTINUE
C
C***  INITIALIZE DELETE NAMELIST FLAGS
C
      DO 1050 I=1,12
         DFLGG(I)=.FALSE.
 1050 CONTINUE
C
      DO 1060 I=1,37
         LDUMPF(I)=.FALSE.
 1060 CONTINUE
C
C***  DO NOT RE-INITIALIZE LSOSE OR LHYPER IF NOT FIRST CASE
C
      IF(CASE .EQ. 1)LSOSE=.FALSE.
      IF(CASE .EQ. 1)LHYPER=.FALSE.
C
      PRESUR=.FALSE.
C
      LHYBRD=.FALSE.
C
C***  INITIALIZE INCRMT COREC FACTORS IF CASE = 1
C
      IF(CASE.NE.1) GO TO 1080
      DO 1070 I=1,128
          COREC(I)=UNUSED
 1070 CONTINUE
 1080 CONTINUE
      RETURN
      END
