      SUBROUTINE BDXCPN(TNOSE,MACH,FNOSE,FCENTR,XCP)
C
C***  SUBROUTINE TO INTERPOLATE THE LONGITUDINAL CENTER OF PRESSURE
C***  DATA FOR CONE-CYLINDERS OR OGIVE-CYLINDERS IN THE TRANSONIC
C***  MACH REGIME
C
C***  DATA FROM MBB REPORT TN WE 2 - 97/69 AND WE 12 - 88/70
C
C***  VALID RANGES - MACH NUMBER 0.8 TO 1.2 (0.8 DATA USED AT LOWER M)
C***                 FNOSE         0. TO 2.5 (CONES)
C***                               .5 TO 3.5 (OGIVES)
C***                 FCENTR        6. TO 15. (CONES)
C***                               6. TO 18./20. (OGIVES)
C***  NOTE - SPHERICAL NOSE BLUNTNESS ASSUMED NEGLIGIBLE
C
C***  INPUTS
C
C  TNOSE -- NOSE TYPE, 0=CONICAL, 1=OGIVAL
C   MACH -- FREESTREAM MACH NUMBER
C  FNOSE -- NOSE THEORETICAL FINENESS RATIO (LENGTH/DMAX)
C  FCENTR-- CYLINDRICAL SECTION FINENESS RATIO
C
C***  OUTPUT
C
C    XCP -- LONGITUDINAL CENTER OF PRESSURE, CALIBERS FROM NOSE
C
      REAL MACH
C
      DIMENSION CONE00(15),CONE05(27),CONE15(18),CONE25(27)
      DIMENSION OGIV05(36),OGIV15(36),OGIV25(36),OGIV35(36)
      DIMENSION TMACH(9),TFA1(3),TFA2(4),TFA3(4)
      DIMENSION IROUT(2),XX(5),YY(5)
C
      DATA TMACH / 0.80,0.85,0.90,0.95,1.00,1.05,1.10,1.15,1.20 /
      DATA TFA1 / 6., 10., 15. /
      DATA TFA2 / 6., 10., 15., 20. /
      DATA TFA3 / 6., 10., 15., 18. /
C
C***  FOLLOWING DATA IS XCP+.4*FCENTR
C
      DATA CONE00 /
     * 5.80000, 6.00000, 6.20000, 5.65000, 4.95000, 8.50000, 8.95000,
     * 9.30000, 8.80000, 7.90000,11.65000,11.80000,11.90000,11.80000,
     *11.50000
     * /
      DATA CONE05 /
     * 5.08381, 5.69442, 5.49078, 3.85589, 3.23294, 3.27590, 3.61509,
     * 3.28823, 2.78896, 7.73856, 8.25078, 8.36790, 7.27584, 6.48003,
     * 6.54796, 6.59137, 6.75799, 6.65320,11.05799,11.42155,11.43998,
     *10.59482,10.76145,10.60731, 9.29288, 9.18824, 9.33004
     * /
      DATA CONE15 /
     * 4.42222, 4.41278, 4.32894, 4.26954, 4.23457, 4.27435, 6.97637,
     * 6.91696, 6.90715, 7.02023, 7.18510, 7.19936,10.20515,10.12022,
     *10.23440,10.67057,11.23072,11.24644
     * /
      DATA CONE25 /
     * 4.89600, 4.94857, 4.92601, 5.00366, 5.00613, 5.00865, 4.96101,
     * 4.83844, 4.66557, 7.45217, 7.50462, 7.58227, 7.83494, 7.98747,
     * 8.11528, 8.21770, 8.49558, 8.59794,10.83477,10.88733,11.03986,
     *11.56770,12.12068,12.09825,11.50028,10.95241,10.45459
     * /
      DATA OGIV05 /
     * 3.81484, 3.69015, 3.49175, 3.39155, 3.26686, 3.26480, 3.48356,
     * 3.28531, 2.94005, 6.26525, 6.14051, 5.96680, 6.03830, 6.23247,
     * 6.32865, 5.98314, 5.41686, 5.09603, 9.89237,10.18484,10.06005,
     *10.05809,10.27685,10.17669,10.05200, 9.68191, 9.21382,13.24970,
     *13.07594,13.02490,13.53819,14.14943,14.17190,13.60552,13.28444,
     *13.20897
     * /
      DATA OGIV15 /
     * 3.96916, 3.97383, 4.02784, 4.05729, 4.01239, 3.86904, 3.97228,
     * 3.90316, 3.63578, 6.32006, 6.39874, 6.47741, 6.70399, 6.80734,
     * 6.61465, 6.74244, 6.74722, 6.55431,10.07928,10.20719,10.38431,
     *10.70969,11.01050,10.96572,10.87182,10.40766, 9.79558,13.73960,
     *13.81839,13.99541,14.34545,14.94242,15.09533,14.48326,14.09299,
     *13.94918
     * /
      DATA OGIV25 /
     * 4.50461, 4.57200, 4.61425, 4.73118, 4.67396, 4.34200, 4.35929,
     * 4.37657, 4.26981, 6.98875, 7.08110, 7.22355, 7.46547, 7.63270,
     * 7.47546, 7.31802, 6.96092, 6.50473,10.29355,10.38608,10.72765,
     *11.19458,11.53614,11.37890,10.89719,10.36537, 9.85869,12.40629,
     *12.67316,13.13990,13.58187,13.82341,13.74086,13.78310,14.07494,
     *13.91788
     * /
      DATA OGIV35 /
     * 4.80873, 4.81294, 4.99310, 5.34867, 5.45341, 5.10604, 5.00994,
     * 4.88862, 4.71678, 7.57702, 7.58123, 7.63555, 7.94112, 8.27180,
     * 7.97475, 8.02907, 8.00828, 7.76134,11.12525,11.25478,11.45993,
     *12.01667,12.57340,12.37677,12.23045,12.23445,12.06282,13.28955,
     *13.29366,13.37318,13.80407,14.83790,15.31911,15.49906,15.75474,
     *15.35685
     * /
C
      AMACH=MACH
      IF(MACH .LT. 0.8)AMACH=0.8
C
C***  DETERMINE NOSE TYPE
C
      IF(TNOSE .NE. 0.)GO TO 1000
C
C***  HERE FOR CONICAL NOSES
C
C***  INTERPOLATE AT EACH NOSE FINENESS RATIO
C
      CALL MVLOOK(1,3,5,15,0.0,TFA1,TMACH,CONE00,AMACH,FCENTR,0.0,XCP00)
      CALL MVLOOK(1,3,9,27,0.5,TFA1,TMACH,CONE05,AMACH,FCENTR,0.5,XCP05)
      CALL MVLOOK(1,3,6,18,1.5,TFA1,TMACH,CONE15,AMACH,FCENTR,1.5,XCP15)
      CALL MVLOOK(1,3,9,27,2.5,TFA1,TMACH,CONE25,AMACH,FCENTR,2.5,XCP25)
C
C***  NOW LINEARILY INTERPOLATE AT FNOSE
C
      XX(1)=0.0
      XX(2)=0.5
      XX(3)=1.5
      XX(4)=2.5
C
C ... REMOVE .4*FCENTR FROM DATA
C
      YY(1)=XCP00-.4*FCENTR
      YY(2)=XCP05-.4*FCENTR
      YY(3)=XCP15-.4*FCENTR
      YY(4)=XCP25-.4*FCENTR
      CALL LNTRP(XX,YY,4,FNOSE,XCP)
C
      GO TO 1010
 1000 CONTINUE
C
C***  OGIVAL (AND OTHER) SHAPES
C
C***  INTERPOLATE AT EACH NOSE FINENESS RATIO
C
      CALL MVLOOK(1,3,5,15,0.0,TFA1,TMACH,CONE00,AMACH,FCENTR,0.0,XCP00)
      CALL MVLOOK(1,4,9,36,0.5,TFA2,TMACH,OGIV05,AMACH,FCENTR,0.5,XCP05)
      CALL MVLOOK(1,4,9,36,1.5,TFA2,TMACH,OGIV15,AMACH,FCENTR,1.5,XCP15)
      CALL MVLOOK(1,4,9,36,2.5,TFA3,TMACH,OGIV25,AMACH,FCENTR,2.5,XCP25)
      CALL MVLOOK(1,4,9,36,3.5,TFA3,TMACH,OGIV35,AMACH,FCENTR,3.5,XCP35)
C
C***  NOW LINEARILY INTERPOLATE AT FNOSE
C
      XX(1)=0.0
      XX(2)=0.5
      XX(3)=1.5
      XX(4)=2.5
      XX(5)=3.5
C
C ... REMOVE .4*FCENTR FROM DATA
C
      YY(1)=XCP00-.4*FCENTR
      YY(2)=XCP05-.4*FCENTR
      YY(3)=XCP15-.4*FCENTR
      YY(4)=XCP25-.4*FCENTR
      YY(5)=XCP35-.4*FCENTR
      CALL LNTRP(XX,YY,5,FNOSE,XCP)
C
 1010 CONTINUE
C
      RETURN
      END
