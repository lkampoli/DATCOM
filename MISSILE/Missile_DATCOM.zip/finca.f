      SUBROUTINE FINCA(IM,NFIN)
C
C***  FIN ALONE AXIAL FORCE EXECUTIVE ROUTINE
C
      COMMON /GEOFS1/ GFIN1(188)
      COMMON /FSET1/  FDATA1(399)
      COMMON /F1WORK/ F1WK(290)
      COMMON /SFIN1/  CFIN1(220)
C
      COMMON /GEOFS2/ GFIN2(188)
      COMMON /FSET2/  FDATA2(399)
      COMMON /F2WORK/ F2WK(290)
      COMMON /SFIN2/  CFIN2(220)
C
      COMMON /GEOFS3/ GFIN3(188)
      COMMON /FSET3/  FDATA3(399)
      COMMON /F3WORK/ F3WK(290)
      COMMON /SFIN3/  CFIN3(220)
C
      COMMON /GEOFS4/ GFIN4(188)
      COMMON /FSET4/  FDATA4(399)
      COMMON /F4WORK/ F4WK(290)
      COMMON /SFIN4/  CFIN4(220)
C
C***  COMPUTE FINSET 1 AXIAL FORCE DATA
C
      IF(NFIN .EQ. 1)CALL FINXCA(1,IM,GFIN1,FDATA1,F1WK,CFIN1)
C
C***  COMPUTE FINSET 2 AXIAL FORCE DATA
C
      IF(NFIN .EQ. 2)CALL FINXCA(2,IM,GFIN2,FDATA2,F2WK,CFIN2)
C
C***  COMPUTE FINSET 3 AXIAL FORCE DATA
C
      IF(NFIN .EQ. 3)CALL FINXCA(3,IM,GFIN3,FDATA3,F3WK,CFIN3)
C
C***  COMPUTE FINSET 4 AXIAL FORCE DATA
C
      IF(NFIN .EQ. 4)CALL FINXCA(4,IM,GFIN4,FDATA4,F4WK,CFIN4)
C
      RETURN
      END
