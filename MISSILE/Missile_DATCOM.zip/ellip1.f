      FUNCTION ELLIP1(CAYSQ)
C
C***  COMPUTE THE COMPLETE ELLIPTIC INTEGRAL OF THE FIRST
C***  KIND USING A POLYNOMIAL EXPANSION
C
C***  INPUT
C
C   CAYSQ - VALUE FOR K SQUARED (MULTIPLIER TO SIN(PHI)**2)
C
C         FUNCTION INTEGRATED=1./SQRT(1.-CAYSQ*SIN(PHI)**2)
C               OVER THE RANGE OF PHI FROM 0 TO PI/2
C
C***  REFERENCE - HANDBOOK OF MATH FUNCTIONS
C                 BY ABRAMOVITZ AND STEGUN
C                 PAGE 591, EQUATION 17.3.34
C
      ELLIP1=1000.
C
      IF(CAYSQ .GE. 1.)GO TO 1000
C
      CAY=1.-CAYSQ
C
      ELLIP1=1.38629436112+.09666344259*CAY+.03590092383*CAY**2+
     1                     .03742563713*CAY**3+.01451196212*CAY**4+
     2       ALOG(1./CAY)*(.5+.12498593597*CAY+.06880248576*CAY**2+
     3                     .03328355346*CAY**3+.00441787012*CAY**4)
C
 1000 CONTINUE
C
      RETURN
      END
