      SUBROUTINE RFINS(NSET,LFIN1,LFIN2,LFIN3,LFIN4,
     1                 FIN1,FIN2,FIN3,FIN4)
C
C***  FIN INPUTS
C
      DIMENSION FIN1(389),FIN2(389),FIN3(389),FIN4(389)
C
      LOGICAL LFIN1,LFIN2,LFIN3,LFIN4
C
      GO TO (1000, 1010, 1020, 1030), NSET
C
 1000 CALL RFIN1(LFIN1,FIN1)
      GO TO 1040
C
 1010 CALL RFIN2(LFIN2,FIN2)
      GO TO 1040
C
 1020 CALL RFIN3(LFIN3,FIN3)
      GO TO 1040
C
 1030 CALL RFIN4(LFIN4,FIN4)
C
 1040 CONTINUE
C
      RETURN
      END
