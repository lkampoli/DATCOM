      SUBROUTINE INITZ(CASE,SAVE)
C
C***  SUBROUTINE TO INITIALIZE ALL PROGRAM ARRAYS TO UNUSED
C
      INTEGER CASE
      LOGICAL SAVE
C
C  BODY ARRAYS
C
      CALL INZBOD
C
C  FIN ARRAYS
C
      CALL INZFIN(CASE,SAVE)
C
C  PROGRAM FLAGS
C
      CALL INZFLG(CASE,SAVE)
C
C  FLIGHT CONDITIONS
C
      CALL INZFLC
C
C  IDEAL OUTPUT MATRIX (IOM)
C
      CALL INZIOM
C
C  REFERENCE QUANTITIES
C
      CALL INZREF
C
C  TRIM ARRAYS
C
      CALL INZTRM
C
      RETURN
      END
