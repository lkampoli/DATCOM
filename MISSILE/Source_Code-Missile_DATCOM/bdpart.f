      SUBROUTINE BDPART(IM,CN1,CN2,CN3,CN4,CN5,CN6,CN7,CN8)
C
C***  PRINT BODY ALONE PARTIAL OUTPUT DATA
C
      COMMON /CONST/ PI,RAD,UNUSED,KAND
      COMMON /FLC/   NALPHA,ALPHA(20),BETA,PHI,NMACH,MACH(20),
     1               ALT(20),REN(20),VINF(20),TINF(20),PINF(20)
C
      REAL NALPHA,NMACH,MACH
C
      COMMON /ABODIN/ BDATA(236),DEXIT,BASE,ADUM1(61),PROTUB,ADUM2(581)
      COMMON /BDWORK/ CNP(20),CMP(20),CNV(20),CMV(20),CAPR(20),
     1                CAF(20),CAB(20),ETA,CDC(20),CAPROT(20),
     2                BOTDCA(20),BOTDCN(20),BOTDCM(20)
C
      COMMON /SBODY/ CN(20),CM(20),CA(20),CY(20),CLN(20),CLL(20),
     1               CNA(20),CMA(20),CYB(20),CNB(20),CLB(20)
C
      LOGICAL NMF,NAF,NDUM,BASE,PROTUB
C
      CALL HEADR9
C
      WRITE(6,1030)
C
      CALL PRFLC9(IM,NDUM,NMF,NAF)
C
      WRITE(6,1120)
C
      NALP=NALPHA+0.5
C
      DO 1000 I=1,NALP
        IF(DEXIT .EQ. UNUSED) THEN
          CAALP=CA(I)-CAPR(I)-CAF(I)-CAPROT(I)-BOTDCA(I)
        ELSE
          CAALP=CA(I)-CAB(I)-CAPR(I)-CAF(I)-CAPROT(I)-BOTDCA(I)
        ENDIF
        IF (PROTUB.AND.BASE.AND.(MACH(IM).GT.1.2)) THEN
            WRITE(6,1130) ALPHA(I),CAF(I),CAPR(I),
     &               CAB(I),CAPROT(I),BOTDCA(I),CAALP
        ELSEIF (PROTUB) THEN
            WRITE(6,1140) ALPHA(I),CAF(I),CAPR(I),
     &               CAB(I),CAPROT(I),CAALP
        ELSEIF (BASE.AND.(MACH(IM).GT.1.2)) THEN
            WRITE(6,1150) ALPHA(I),CAF(I),CAPR(I),
     &               CAB(I),BOTDCA(I),CAALP
        ELSE
            WRITE(6,1160) ALPHA(I),CAF(I),CAPR(I),
     &               CAB(I),CAALP
        ENDIF
 1000 CONTINUE
C
      IF(DEXIT .EQ. UNUSED)WRITE(6,1080)
C
      WRITE(6,1090)ETA
C
      WRITE(6,1170)
      DO 1010 I=1,NALP
        IF (BASE.AND.(MACH(IM).GT.1.2)) THEN
          WRITE(6,1180) ALPHA(I),CNP(I),CNV(I),BOTDCN(I),
     &                 CMP(I),CMV(I),BOTDCM(I),CDC(I)
        ELSE
          WRITE(6,1190) ALPHA(I),CNP(I),CNV(I),CMP(I),CMV(I),CDC(I)
        ENDIF
 1010 CONTINUE
C
      IF(CN1 .EQ. 1. .AND. CN2 .EQ. 1. .AND. CN3 .EQ. 1. .AND.
     1   CN4 .EQ. 1. .AND. CN5 .EQ. 1. .AND. CN6 .EQ. 1. .AND.
     2   CN7 .EQ. 1. .AND. CN8 .EQ. 1.)GO TO 1020
C
      WRITE(6,1100)CN1,CN2,CN3,CN4,CN5,CN6,CN7,CN8
C
 1020 CONTINUE
C
 1030 FORMAT(4X,25X,'BODY ALONE PARTIAL OUTPUT',/)
 1080 FORMAT(4X,'NOTE - BASE DRAG IS NOT INCLUDED',
     1 ' IN THE AXIAL FORCE CALCULATIONS',/)
 1090 FORMAT(/,4X,17X,'CROSS FLOW DRAG PROPORTIONALITY FACTOR =',F8.5,/)
 1100 FORMAT(/,4X,20X,'ELLIPTICAL BODY CORRECTION FACTORS',/
     1  4X,21X,'*** IN THE HORIZONTAL PLANE ***',/
     2  4X,'CN/CNO SLENDER BODY THEORY=',F8.3, 
     3  4X,'CN/CNO NEWTONIAN THEORY   =',F8.3,/
     4  4X,'CM/CMO SLENDER BODY THEORY=',F8.3, 
     5  4X,'CM/CMO NEWTONIAN THEORY   =',F8.3,/
     6       4X,21X,'*** IN THE VERTICAL PLANE ***',/
     7  4X,'CN/CNO SLENDER BODY THEORY=',F8.3, 
     8  4X,'CN/CNO NEWTONIAN THEORY   =',F8.3,/
     9  4X,'CM/CMO SLENDER BODY THEORY=',F8.3, 
     A  4X,'CM/CMO NEWTONIAN THEORY   =',F8.3,/)
 1120 FORMAT(4X,2X,'ALPHA',4X,'CA-FRIC',2X,'CA-PRES/WAVE',1X,
     1 'CA-BASE',4X,'CA-PROT',5X,'CA-SEP',5X,'CA-ALP')
 1130 FORMAT(4X,F7.2,6F11.4)
 1140 FORMAT(4X,F7.2,4F11.4,11X,F11.4)
 1150 FORMAT(4X,F7.2,3F11.4,11X,2F11.4)
 1160 FORMAT(4X,F7.2,3F11.4,11X,11X,F11.4)
 1170 FORMAT(4X,2X,'ALPHA',4X,'CN-POTEN',2X,'CN-VISC',3X,
     1 'CN-SEP',4X,'CM-POTEN',2X,'CM-VISC',
     2  3X,'CM-SEP',4X,'CDC')
 1180 FORMAT(4X,F7.2,6F10.3,F8.3)
 1190 FORMAT(4X,F7.2,2F10.3,10X,2F10.3,10X,F8.3)
C
      RETURN
      END
