      SUBROUTINE PRITRM(IM,NSET,DELTAB,PANL1,PANL2,PANL3,PANL4,PANL5,
     1 PANL6,PANL7,PANL8,DELTRM,CNTRM,CMTRM,CATRM,CYTRM,CLNTRM,CLLTRM)
C
C***  PRINT TRIMMED STATIC AERODYNAMICS COMPUTED
C
      COMMON /FLC/    NALPHA,ALPHA(20),BETA,PHI,NMACH,MACH(20),
     1                ALT(20),REN(20),VINF(20),TINF(20),PINF(20)
C
      COMMON /FSET1/  FS1(111),APAN1,FS12(287)
      COMMON /FSET2/  FS2(111),APAN2,FS22(287)
      COMMON /FSET3/  FS3(111),APAN3,FS32(287)
      COMMON /FSET4/  FS4(111),APAN4,FS42(287)
      COMMON /CONST/  PI,RAD,UNUSED,KAND
C
      DIMENSION DELTRM(20),CNTRM(20),CMTRM(20),CATRM(20),
     1 CYTRM(20),CLNTRM(20),CLLTRM(20),CLTRM(20),CDTRM(20),
     2 FORM(37),ICONT(9)
      DIMENSION DELTAB(10),NPAN(4)
C
      REAL NALPHA,NMACH,MACH
      CHARACTER*4 FORM,NDM,NNA
C
      LOGICAL NM,NA,NB,PANL1,PANL2,PANL3,PANL4,PANL5,PANL6,PANL7,PANL8
C
      DATA I1,I2,I3,I4,I5,I6,I7,I8 / 1, 2, 3, 4, 5, 6, 7, 8 /
       DATA ICONT / 10,11,11,11,11,11,11,11,11 /
      DATA FORM /
     1 '(   ','13X,','  F7','.2, ',
     2 ' 2X,',' 1X,','  F1','0.4,',
     3 ' 2X,',' 1X,','  F1','0.4,',
     4 ' 2X,',' 1X,','  F1','0.4,',
     5 ' 2X,',' 1X,','  F1','0.4,',
     6 ' 2X,',' 1X,','  F1','0.4,',
     7 ' 2X,',' 1X,','  F1','0.4,',
     8 ' 2X,',' 1X,','  F1','0.4,',
     9 ' 2X,',' 1X,','  F1','0.4,',
     A '/)  '/
C
      DATA NDM /'*NT*'/
      DATA NNA /'*NT*'/
C
      NALP=NALPHA+0.5
      NPAN(1)=APAN1+.5
      NPAN(2)=APAN2+.5
      NPAN(3)=APAN3+.5
      NPAN(4)=APAN4+.5
C
      DO 500 I=1,NALP
      CALL CLOCD(ALPHA(I),CNTRM(I),CATRM(I),CLTRM(I),CDTRM(I),DUMM)
      IF(DELTRM(I).EQ.UNUSED) CLTRM(I)=UNUSED
      IF(DELTRM(I).EQ.UNUSED) CDTRM(I)=UNUSED
  500 CONTINUE
      CALL HEADR9
      WRITE(6,1010)
      CALL PRFLC9(IM,NB,NM,NA)
      WRITE(6,1020)
      DO 600 I=1,NALP
      IF(CLTRM(I).NE.UNUSED)WRITE(6,1500) ALPHA(I),DELTRM(I),CLTRM(I),
     1   CDTRM(I),CNTRM(I),CATRM(I)
      IF(CLTRM(I).EQ.UNUSED)WRITE(6,1600) ALPHA(I),
     1                   NDM,NDM,NDM,NDM,NDM
  600 CONTINUE
C
      WRITE(6,1040)NSET,DELTAB(1),DELTAB(10)
         IF(PANL1)WRITE(6,1050)I1
         IF(.NOT. PANL1)WRITE(6,1060)I1
      IF(NPAN(NSET) .LE. 1)GO TO 1005
         IF(PANL2)WRITE(6,1050)I2
         IF(.NOT. PANL2)WRITE(6,1060)I2
      IF(NPAN(NSET) .LE. 2)GO TO 1005
         IF(PANL3)WRITE(6,1050)I3
         IF(.NOT. PANL3)WRITE(6,1060)I3
      IF(NPAN(NSET) .LE. 3)GO TO 1005
         IF(PANL4)WRITE(6,1050)I4
         IF(.NOT. PANL4)WRITE(6,1060)I4
      IF(NPAN(NSET) .LE. 4)GO TO 1005
         IF(PANL5)WRITE(6,1050)I5
         IF(.NOT. PANL5)WRITE(6,1060)I5
      IF(NPAN(NSET) .LE. 5)GO TO 1005
         IF(PANL6)WRITE(6,1050)I6
         IF(.NOT. PANL6)WRITE(6,1060)I6
      IF(NPAN(NSET) .LE. 6)GO TO 1005
         IF(PANL7)WRITE(6,1050)I7
         IF(.NOT. PANL7)WRITE(6,1060)I7
      IF(NPAN(NSET) .LE. 7)GO TO 1005
         IF(PANL8)WRITE(6,1050)I8
         IF(.NOT. PANL8)WRITE(6,1060)I8
 1005 IF(NM)WRITE(6,1030)
C
 1010 FORMAT(18X,'STATIC AERODYNAMIC COEFFICIENTS TRIMMED IN PITCH',/)
 1020 FORMAT(4X,5X,'ALPHA',5X,'DELTA',7X,'CL',8X,'CD',8X,
     1       'CN',8X,'CA')
 1030 FORMAT(4X,'NOTE - *NT* PRINTED WHEN NO TRIM POINT COULD BE',
     1  ' FOUND')
 1040 FORMAT(4X,'PANELS FROM FIN SET',I2,' WERE DEFLECTED OVER THE',
     1 ' RANGE',F7.2,' TO',F7.2,' DEG')
 1050 FORMAT(4X,'PANEL',I2,' WAS VARIED')
 1060 FORMAT(4X,'PANEL',I2,' WAS FIXED')
 1500 FORMAT(4X,F10.2,F10.2,4F10.3)
 1600 FORMAT(4X,F10.2,5X,A4,6X,A4,6X,A4,6X,A4,6X,A4)
C
      RETURN
      END
