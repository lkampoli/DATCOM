      SUBROUTINE PLTUT9(IM,DELTAB,DELTA1,DELTA2,DELTA3,DELTA4,
     1 NSET,PANL1,PANL2,PANL3,PANL4)
C
C***  FORTRAN 90 VERSION
C
C***  PRINT UNTRIMMED STATIC AERODYNAMICS TO PLOT FILE (UNIT 3)
C
      COMMON /FLC/    NALPHA,ALPHA(20),BETA,PHI,NMACH,MACH(20),
     1                ALT(20),REN(20),VINF(20),TINF(20),PINF(20)
      COMMON /UTRIMD/CNT(200),CMT(200),CAT(200),CYT(200),
     1               CLNT(200),CLLT(200)
C
      COMMON /FSET1/ FS1(111),APAN1,FS12(287)
      COMMON /FSET2/ FS2(111),APAN2,FS22(287)
      COMMON /FSET3/ FS3(111),APAN3,FS32(287)
      COMMON /FSET4/ FS4(111),APAN4,FS42(287)
C
      COMMON /LOGIC/ LDMPCS,LDAMP,LBUILD,LNACA,LDERDG,
     1               LDERRD,LPART,LNAME,LPLOT,
     2               LFLT,LREFQ,LAXIS,LFIN1,LFIN2,LFIN3,LFIN4,
     3               LDEFL,LTRIM,LDIMIN,LDIMFT,LDIMCM,LDIMM,LELLB,
     4               LINLET,LEXPR,LICRMT,LSPIN,LARBOD
      LOGICAL        LDMPCS,LDAMP,LBUILD,LNACA,LDERDG,
     1               LDERRD,LPART,LNAME,LPLOT,
     2               LFLT,LREFQ,LAXIS,LFIN1,LFIN2,LFIN3,LFIN4,
     3               LDEFL,LTRIM,LDIMIN,LDIMFT,LDIMCM,LDIMM,LELLB,
     4               LINLET,LEXPR,LICRMT,LSPIN,LARBOD
C
      DIMENSION DELTAB(10),DELTA1(8),DELTA2(8),DELTA3(8),DELTA4(8)
C
      REAL NALPHA,NMACH,MACH
C
      LOGICAL NA,NB,NC,PANL1,PANL2,PANL3,PANL4
C
      DATA I1,I2,I3,I4 / 1, 2, 3, 4 /
C
      NALP=NALPHA+0.5
C
      DO 1010 ID=1,10
         WRITE(3,1200) MACH(IM)
         DO 1000 I=1,NALP
         WRITE(3,1250)   ALPHA(I), CNT(I+(ID-1)*20),
     1           CMT(I+(ID-1)*20), CAT(I+(ID-1)*20),
     2           CYT(I+(ID-1)*20),CLNT(I+(ID-1)*20),
     3          CLLT(I+(ID-1)*20), DELTAB(ID)
 1000    CONTINUE
 1010 CONTINUE
C
 1200 FORMAT('ZONE T="UNTRIMMED MACH=',F6.2,'"')
 1250 FORMAT(F7.2,7F10.4)
      RETURN
      END
