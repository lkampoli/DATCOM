      SUBROUTINE CVRFIN(ISIGN)
C
C***  ROUTINE TO CONVERT THE DATA IN THE FIN ALONE
C***  DATA BLOCKS BETWEEN THE USER AND FOOT SYSTEM OF UNITS
C
      COMMON /FSET1/  FDATA(399)
      COMMON /FSET2/  FDATA2(399)
      COMMON /FSET3/  FDATA3(399)
      COMMON /FSET4/  FDATA4(399)
C
      COMMON /GEOFS1/ GFIN(188)
      COMMON /GEOFS2/ GFIN2(188)
      COMMON /GEOFS3/ GFIN3(188)
      COMMON /GEOFS4/ GFIN4(188)
C
      COMMON /INCID/  DELTAS(32),XHINGE(4),SKEW(4)
C
      DIMENSION SSPAN(10),LMAXU(10),LFLATU(10),LMAXL(10),
     1          LFLATL(10),CHORD(10),THICKU(10),THICKL(10),
     2          SWEEP(10),STA(10),XLE(10),
     3          XCORD(50),MEAN(50),THICK(50),YUPPER(50),
     4          YLOWER(50),LER(10)
C
      REAL LER,LMAXU,LMAXL,LFLATU,LFLATL,NPANEL,MEAN
C
      EQUIVALENCE (FDATA(1),SECTYP),(FDATA(2),SSPAN(1)),
     1            (FDATA(12),LMAXU(1)),(FDATA(22),LFLATU(1)),
     2            (FDATA(32),LMAXL(1)),(FDATA(42),LFLATL(1)),
     3            (FDATA(52),CHORD(1)),(FDATA(62),THICKU(1)),
     4            (FDATA(72),THICKL(1)),(FDATA(82),SWEEP(1)),
     5            (FDATA(92),STA(1)),(FDATA(102),XLE(1)),
     6            (FDATA(112),NPANEL),(FDATA(113),XCORD(1)),
     7            (FDATA(163),MEAN(1)),(FDATA(213),THICK(1)),
     8            (FDATA(263),YUPPER(1)),(FDATA(313),YLOWER(1)),
     9            (FDATA(363),FINPHI),(FDATA(364),LER(1))
C
      DIMENSION SPLANF(10),CBAR(10),SWEPLE(10),SWEP25(10),
     1          SWEP50(10),SWEP75(10),SWEPTE(10),
     2          XMGCLE(10),XMGC25(10),
     3          XMGC50(10),XMGC75(10),XMGCTE(10),YMEANC(10),
     4          ASPCT(10),TAPER(10),TOVERC(10),XCENT(10)
C
      EQUIVALENCE (GFIN(1),SPLANF(1)),(GFIN(11),CBAR(1)),
     1            (GFIN(21),SWEPLE(1)),(GFIN(31),SWEP25(1)),
     2            (GFIN(41),SWEP50(1)),(GFIN(51),SWEP75(1)),
     3            (GFIN(61),SWEPTE(1)),(GFIN(71),XMGCLE(1)),
     4            (GFIN(81),XMGC25(1)),(GFIN(91),XMGC50(1)),
     5            (GFIN(101),XMGC75(1)),(GFIN(111),XMGCTE(1)),
     6            (GFIN(121),YMEANC(1)),(GFIN(131),ASPCT(1)),
     7            (GFIN(141),TAPER(1)),(GFIN(151),TOVERC(1)),
     8            (GFIN(161),XCENT(1)),(GFIN(171),FINAR),
     9            (GFIN(172),FINTPR),(GFIN(173),FINSPN),
     A            (GFIN(174),FINSP),(GFIN(175),FINWET),
     B            (GFIN(176),TCEFF),(GFIN(177),FINXGC),
     C            (GFIN(178),FINYGC),(GFIN(179),SWAVLE),
     D            (GFIN(180),SWAV25),(GFIN(181),SWAV50),
     E            (GFIN(182),SWAV75),(GFIN(183),SWAVTE),
     F            (GFIN(184),FINCBR),(GFIN(185),DELTAY),
     G            (GFIN(186),DELTAD),(GFIN(187),XCENF),(GFIN(188),XOVC)
C
C***  CONVERT FIN DATA
C
      CALL CONVRT(FDATA(2),10,ISIGN*1)
      CALL CONVRT(FDATA(52),10,ISIGN*1)
      CALL CONVRT(FDATA(102),10,ISIGN*1)
      CALL CONVRT(FDATA(364),10,ISIGN*1)
      CALL CONVRT(GFIN(1),10,ISIGN*2)
      CALL CONVRT(GFIN(11),10,ISIGN*1)
      CALL CONVRT(GFIN(71),60,ISIGN*1)
      CALL CONVRT(GFIN(161),10,ISIGN*1)
      CALL CONVRT(GFIN(173),1,ISIGN*1)
      CALL CONVRT(GFIN(174),2,ISIGN*2)
      CALL CONVRT(GFIN(177),2,ISIGN*1)
      CALL CONVRT(GFIN(184),1,ISIGN*1)
      CALL CONVRT(GFIN(187),1,ISIGN*1)
      CALL CONVRT(FDATA2(2),10,ISIGN*1)
      CALL CONVRT(FDATA2(52),10,ISIGN*1)
      CALL CONVRT(FDATA2(102),10,ISIGN*1)
      CALL CONVRT(FDATA2(364),10,ISIGN*1)
      CALL CONVRT(GFIN2(1),10,ISIGN*2)
      CALL CONVRT(GFIN2(11),10,ISIGN*1)
      CALL CONVRT(GFIN2(71),60,ISIGN*1)
      CALL CONVRT(GFIN2(161),10,ISIGN*1)
      CALL CONVRT(GFIN2(173),1,ISIGN*1)
      CALL CONVRT(GFIN2(174),2,ISIGN*2)
      CALL CONVRT(GFIN2(177),2,ISIGN*1)
      CALL CONVRT(GFIN2(184),1,ISIGN*1)
      CALL CONVRT(GFIN(187),1,ISIGN*1)
      CALL CONVRT(FDATA3(2),10,ISIGN*1)
      CALL CONVRT(FDATA3(52),10,ISIGN*1)
      CALL CONVRT(FDATA3(102),10,ISIGN*1)
      CALL CONVRT(FDATA3(364),10,ISIGN*1)
      CALL CONVRT(GFIN3(1),10,ISIGN*2)
      CALL CONVRT(GFIN3(11),10,ISIGN*1)
      CALL CONVRT(GFIN3(71),60,ISIGN*1)
      CALL CONVRT(GFIN3(161),10,ISIGN*1)
      CALL CONVRT(GFIN3(173),1,ISIGN*1)
      CALL CONVRT(GFIN3(174),2,ISIGN*2)
      CALL CONVRT(GFIN3(177),2,ISIGN*1)
      CALL CONVRT(GFIN3(184),1,ISIGN*1)
      CALL CONVRT(GFIN(187),1,ISIGN*1)
      CALL CONVRT(FDATA4(2),10,ISIGN*1)
      CALL CONVRT(FDATA4(52),10,ISIGN*1)
      CALL CONVRT(FDATA4(102),10,ISIGN*1)
      CALL CONVRT(FDATA4(364),10,ISIGN*1)
      CALL CONVRT(GFIN4(1),10,ISIGN*2)
      CALL CONVRT(GFIN4(11),10,ISIGN*1)
      CALL CONVRT(GFIN4(71),60,ISIGN*1)
      CALL CONVRT(GFIN4(161),10,ISIGN*1)
      CALL CONVRT(GFIN4(173),1,ISIGN*1)
      CALL CONVRT(GFIN4(174),2,ISIGN*2)
      CALL CONVRT(GFIN4(177),2,ISIGN*1)
      CALL CONVRT(GFIN4(184),1,ISIGN*1)
      CALL CONVRT(GFIN(187),1,ISIGN*1)
      CALL CONVRT(XHINGE(1),4,ISIGN*1)
C
      RETURN
      END
