      SUBROUTINE INLETG
C
C*** PRINT THE INLET GEOMETRY INPUT BY THE USER
C
C  WRITTEN BY M. MOORE, MCDONNELL DOUGLAS
C  MODIFIED BY K. BURNS, MCDONNELL DOUGLAS
C
      COMMON /INLETN/ NIN,INTYPE,XINLT,XDIV,HDIV,LDIV,PHI(20),
     1                XI(5),HI(5),WI(5),ICOVER,RAMANG,IAD,MFR(20)
C
      REAL INTYPE,NIN,LDIV
      LOGICAL ICOVER
C
      CALL HEADR9
      WRITE(6,100)
C
      CALL CVRTFT
C
      IF(INTYPE .EQ. 1.)WRITE(6,200)
      IF(INTYPE .EQ. 2.)WRITE(6,300)
      IF(INTYPE .EQ. 3.)WRITE(6,400)
      IF (ICOVER) THEN
        WRITE(6,1600)
      ELSE
        WRITE(6,1700)
      ENDIF
      IF (.NOT.ICOVER) THEN
        WRITE(6,1800) RAMANG
      ENDIF
      NUMI=NIN+0.5
      WRITE(6,500) NUMI
      WRITE(6,600)
      WRITE(6,700) (PHI(I),I=1,NUMI)
      WRITE(6,800) XINLT
      IF (INTYPE .EQ. 3.) THEN
        WRITE(6,900)
        WRITE(6,1000) XI(1),WI(1),XI(2),WI(2),XI(3),WI(3),
     1                XI(4),WI(4),XI(5),WI(5)
      ELSE
        WRITE(6,1100)
        WRITE(6,1200) XI(1),WI(1),HI(1),XI(2),WI(2),HI(2),
     1                XI(3),WI(3),HI(3),XI(4),WI(4),HI(4),
     2                XI(5),WI(5),HI(5)
      ENDIF
      IF(HDIV .GT. 0.0)THEN
        WRITE(6,1300) XDIV
        WRITE(6,1400) LDIV
        WRITE(6,1500) HDIV
      ENDIF
C
      CALL CVRTUS
C
  100 FORMAT(31X,'INLET GEOMETRY'/)
  200 FORMAT(/,12X,'INLET IS A TOP MOUNTED TWO-DIMENSIONAL TYPE')
  300 FORMAT(/,12X,'INLET IS A SIDE MOUNTED TWO-DIMENSIONAL TYPE')
  400 FORMAT(/,12X,'INLET IS AN AXISYMMETRIC TYPE')
  500 FORMAT(/,12X,'NUMBER OF INLETS =',I2)
  600 FORMAT(/,12X,'INLET ANGULAR ROLL POSITIONS FROM TOP VERTICAL ',
     1 'CENTER (DEG)',/,17X,'(SAME CONVENTION AS FIN ROLL POSITIONS)')
  700 FORMAT(12X,20(F8.1))
  800 FORMAT(/,12X,'LONGITUDINAL DISTANCE FROM MISSILE NOSE TIP TO',/,
     1         12X,'INLET LEADING EDGE =',F7.2)
  900 FORMAT(/,12X,'INLET POSITIONS RELATIVE TO THE INLET TIP',
     1    /,12X,'    POSITION      LONGITUDINAL        DIAMETER')
 1000 FORMAT(12X,'   INLET TIP',8X,F10.3,6X,F10.3,/,30X,
     1         'COWL LIP LEADING EDGE',1X,F10.3,6X,F10.3,/,32X,
     2           ' MID BODY START',5X,F10.3,6X,F10.3,/,32X,
     3           ' BOATTAIL START',5X,F10.3,6X,F10.3,/,32X,
     4           '  BOATTAIL END',6X,F10.3,6X,F10.3)
 1100 FORMAT(/,19X,'INLET POSITIONS RELATIVE TO THE LEADING EDGE',
     1/,19X,'POSITION            LONGITUDINAL     WIDTH       HEIGHT')
 1200 FORMAT(12X,' TOP LIP LEADING EDGE',5X,F10.3,3X,F10.3,3X,F10.3,/,
     1     12X,' COWL LIP LEADING EDGE ',3X,F10.3,3X,F10.3,3X,F10.3,/,
     2     12X,'    MID BODY START',8X,F10.3,3X,F10.3,3X,F10.3,/,
     3     12X,'    BOATTAIL START',8X,F10.3,3X,F10.3,3X,F10.3,/,
     4     12X,'     BOATTAIL END',9X,F10.3,3X,F10.3,3X,F10.3)
 1300 FORMAT(/,12X,'LONGITUDINAL DISTANCE FROM INLET LEADING EDGE TO',/,
     1      12X,'DIVERTER LEADING EDGE =',F7.2)
 1400 FORMAT(/,12X,'DIVERTER LENGTH =',F7.2)
 1500 FORMAT(/,12X,'HEIGHT OF DIVERTER LEADING EDGE =',F7.2)
 1600 FORMAT(/,12X,'THE INLETS ARE COVERED')
 1700 FORMAT(/,12X,'THE INLETS ARE OPEN')
 1800 FORMAT(/,12X,'EXTERNAL COMPRESSION RAMP ANGLE (DEG) =',F7.2)
C
      RETURN
      END
