      SUBROUTINE DMPARY(ITYPE,ARRAY,NPT,NAME,NLET)
C
C***  SUBROUTINE TO PRINT THE CONTENTS OF AN ARRAY
C
C  ITYPE -- ARRAY TYPE TO BE DUMPED
C           0 = FLOATING POINT
C           1 = INTEGER
C           2 = LOGICAL
C           3 = HOLLERITH
C  ARRAY -- ARRAY TO BE DUMPED
C    NPT -- DIMENSION OF ARRAY
C   NAME -- NAME OF ARRAY TO BE PRINTED
C   NLET -- NUMBER OF LETTERS IN NAME
C
      DIMENSION ARRAY(NPT),IFOR(8),AFOR(4),BFOR(4)
      DIMENSION IEXP(3),IINT(3),ILOG(3),IHOL(3)
C
      CHARACTER*4 AFOR,BFOR,IEXP,IINT,ILOG,IHOL,IFOR,NAME
C
      DATA IFOR / '    ','    ',',1H(',',I3,','2H)=','    ',
     1            '    ','    '/
C
      DATA AFOR / '(5(5','(5(4','(5(3','(5(2'/
      DATA BFOR / 'X,A1','X,A2','X,A3','X,A4'/
C
      DATA IEXP / '1P, ',' E12','.5))'/
      DATA IINT / '3X, ','I6, ','3X))'/
      DATA ILOG / '2X, ','L1, ','9X))'/
      DATA IHOL / '4X, ','A4, ','4X))'/
C
C***  SET FORMAT FOR THE NUMBER OF LETTERS IN THE NAME AND PRINT
C
      IFOR(1)=AFOR(NLET)
      IFOR(2)=BFOR(NLET)
C
C***  SUBSTITUTE FOR TYPE OF OUTPUT DESIRED
C
C   FLOATING POINT
C
      IF(ITYPE .LE. 0 .OR. ITYPE .GT. 3)IFOR(6)=IEXP(1)
      IF(ITYPE .LE. 0 .OR. ITYPE .GT. 3)IFOR(7)=IEXP(2)
      IF(ITYPE .LE. 0 .OR. ITYPE .GT. 3)IFOR(8)=IEXP(3)
C
C  INTEGER
C
      IF(ITYPE .EQ. 1)IFOR(6)=IINT(1)
      IF(ITYPE .EQ. 1)IFOR(7)=IINT(2)
      IF(ITYPE .EQ. 1)IFOR(8)=IINT(3)
C
C  LOGICAL
C
      IF(ITYPE .EQ. 2)IFOR(6)=ILOG(1)
      IF(ITYPE .EQ. 2)IFOR(7)=ILOG(2)
      IF(ITYPE .EQ. 2)IFOR(8)=ILOG(3)
C
C  HOLLERITH
C
      IF(ITYPE .EQ. 3)IFOR(6)=IHOL(1)
      IF(ITYPE .EQ. 3)IFOR(7)=IHOL(2)
      IF(ITYPE .EQ. 3)IFOR(8)=IHOL(3)
C
C***  WRITE ARRAY
C
      WRITE(6,1000)
      WRITE(6,IFOR)(NAME,I,ARRAY(I),I=1,NPT)
C
 1000 FORMAT(' ')
C
      RETURN
      END
