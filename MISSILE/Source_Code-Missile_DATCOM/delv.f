      SUBROUTINE DELV(ORDER,I,BETA,XXI,TAU,F1,F2,PSIX,PSIR,C,SUM1,SUM2,
     1 SUM3,SUM4,SUM5,SUM6,NN1,NN2,NN3,IK,JL,TTX,TTR,TEMP)
C
C  ROUTINE ADAPTED FROM NSWC AEROPREDICTION CODE
C  ORIGINAL ROUTINE WRITTEN BY F. MOORE, NSWC
C  MODIFIED BY J. JENKINS, WL/FIGC
C
      COMMON /VDARY/ XB(220),RB(220),RBP(220),DMM1(220),C1(220),
     1 RB1(220),RBP1(220),B(220),PSI(220),ZE0X(220),DMM2(220),PHIX(220)
      INTEGER ORDER
      DIMENSION PSIR(220),C(220)
      SUM1=0.
      SUM2=0.
      SUM3=0.
      IF(I.EQ.2) GO TO 1020
      J2=I-1
      DO 1010 J=2,J2
C
C ... SOLUTION OF J.A.S. EQUATION 26, NUMBER 3
C
      TEMP=SUM2
      CALL JAS26(XB(I),BETA,RB(J-1),XB(J-1),XXI,RB(I),TAU,F1,F2,
     1 C(J),SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,-1.,3)
 1010 CONTINUE
      JH=NN1+1
      IF(I.LE.JH) GO TO 1020
      CALL DISC2(I,JH,BETA,SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,3,ORDER)
      J=NN1+1
C
C ... SOLUTION OF J.A.S. EQUATION 26, NUMBER 3
C
      CALL JAS26(XB(I),BETA,RB(J-1),XB(J-1),XXI,RB(I),TAU,F1,F2,
     1 C(J),SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,1.,3)
      JH=NN2+1
      IF(I.LE.JH) GO TO 1020
      CALL DISC2(I,JH,BETA,SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,3,ORDER)
      J=NN2+1
C
C ... SOLUTION OF J.A.S. EQUATION 26, NUMBER 3
C
      CALL JAS26(XB(I),BETA,RB(J-1),XB(J-1),XXI,RB(I),TAU,F1,F2,
     1 C(J),SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,1.,3)
      JH=NN3+1
      IF(I.LE.JH) GO TO 1020
      CALL DISC2(I,JH,BETA,SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,3,ORDER)
      J=NN3+1
C
C ... SOLUTION OF J.A.S. EQUATION 26, NUMBER 3
C
      CALL JAS26(XB(I),BETA,RB(J-1),XB(J-1),XXI,RB(I),TAU,F1,F2,
     1 C(J),SUM1,SUM2,SUM3,SUM4,SUM5,SUM6,1.,3)
 1020 CONTINUE
      IF(ORDER .GT. 1) GO TO 1030
        VX=SUM2
        VR=SUM3
        GO TO 1040
 1030 VX = PSIX + SUM2
      VR = PSIR(I) + SUM3
 1040 CALL AXBNDY(I,BETA,VX,VR,NN1,NN2,NN3,IK,JL,C,TTX,TTR)
      RETURN
      END
