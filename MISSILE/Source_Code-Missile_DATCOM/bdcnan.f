      SUBROUTINE BDCNAN(TNOSE,MACH,FNOSE,FCENTR,CNA)
C
C***  SUBROUTINE TO INTERPOLATE THE NORMAL FORCE COEFFICENT SLOPE
C***  DATA FOR CONE-CYLINDERS OR OGIVE-CYLINDERS IN THE TRANSONIC
C***  MACH REGIME
C
C***  DATA FROM MBB REPORT TN WE 2 - 97/69 AND WE 12 - 88/70
C
C***  VALID RANGES - MACH NUMBER 0.8 TO 1.2 (0.8 DATA USED AT LOWER M)
C***                 FNOSE         0. TO 2.5 (CONES)
C***                               .5 TO 3.5 (OGIVES)
C***                 FCENTR        6. TO 15. (CONES)
C***                               6. TO 18./20. (OGIVES)
C***  NOTE - SPHERICAL NOSE BLUNTNESS ASSUMED NEGLIGIBLE
C
C***  INPUTS
C
C  TNOSE -- NOSE TYPE, 0=CONICAL, 1=OGIVAL
C   MACH -- FREESTREAM MACH NUMBER
C  FNOSE -- NOSE THEORETICAL FINENESS RATIO (LENGTH/DMAX)
C  FCENTR-- CYLINDRICAL SECTION FINENESS RATIO
C
C***  OUTPUT
C
C    CNA -- NORMAL FORCE CURVE SLOPE (/RADIAN)
C
      REAL MACH
C
      DIMENSION CONE00(15),CONE05(27),CONE15(18),CONE25(27)
      DIMENSION OGIV05(36),OGIV15(36),OGIV25(36),OGIV35(36)
      DIMENSION TMACH(9),TFA1(3),TFA2(4),TFA3(4)
      DIMENSION IROUT(2),XX(6),YY(6)
C
      DATA TMACH / 0.80,0.85,0.90,0.95,1.00,1.05,1.10,1.15,1.20 /
      DATA TFA1 / 6., 10., 15. /
      DATA TFA2 / 6., 10., 15., 20. /
      DATA TFA3 / 6., 10., 15., 18. /
C
C***  FOLLOWING DATA IS CNA+.4*FCENTR
C
      DATA CONE00 /
     * 5.40000, 5.45000, 5.65000, 6.10000, 5.90000, 7.75000, 7.75000,
     * 7.80000, 8.20000, 8.05000,10.45000,10.50000,10.60000,10.70000,
     *10.60000
     * /
      DATA CONE05 /
     * 5.17295, 5.56423, 5.48328, 4.64549, 4.40330, 4.58352, 4.86291,
     * 4.72004, 4.45241, 7.44285, 7.89589, 7.95166, 6.98941, 6.34994,
     * 6.57998, 6.63590, 6.74118, 6.75955,10.02525,10.16792,10.14935,
     * 8.72775, 8.73401, 8.92619, 9.32973, 9.21215, 8.72136
     * /
      DATA CONE15 /
     * 4.76328, 4.74995, 4.73657, 4.77251, 4.87005, 5.01683, 6.60941,
     * 6.54678, 6.52113, 6.59403, 6.75313, 6.91225, 8.78772, 8.78669,
     * 8.83495, 8.96941, 9.10390, 9.12750
     * /
      DATA CONE25 /
     * 4.77778, 4.77778, 4.81481, 4.87654, 4.96296, 5.01235, 5.03704,
     * 4.98765, 4.92593, 6.51852, 6.55556, 6.60494, 6.69136, 6.81481,
     * 6.92593, 7.00000, 7.18519, 7.28395, 8.65432, 8.66667, 8.72840,
     * 8.93827, 9.19753, 9.24691, 9.00000, 8.88889, 8.87654
     * /
      DATA OGIV05 /
     * 4.79312, 4.77880, 4.76424, 4.76179, 4.80891, 4.95475, 5.05099,
     * 4.93747, 4.81064, 6.64784, 6.59600, 6.50741, 6.55456, 6.70013,
     * 6.80896, 6.76925, 6.65597, 6.59107, 9.01237, 9.12120, 9.09429,
     * 9.06715, 9.11403, 9.17277, 9.19597, 9.13155, 9.05456,11.20467,
     *11.14027,11.08869,11.20960,11.36704,11.47635,11.43763,11.37320,
     *11.38259
     * /
      DATA OGIV15 /
     * 4.74936, 4.76565, 4.79449, 4.86044, 4.93854, 5.01673, 5.05781,
     * 5.02484, 4.94220, 6.47118, 6.52469, 6.57821, 6.69332, 6.78396,
     * 6.81269, 6.94015, 6.95664, 6.91103, 8.77492, 8.86525, 8.96823,
     * 9.08335, 9.18622, 9.26432, 9.25603, 9.22316, 9.14073,11.07846,
     *11.13197,11.22231,11.32508,11.44030,11.50626,11.54743,11.53935,
     *11.49373
     * /
      DATA OGIV25 /
     * 4.74415, 4.78649, 4.82872, 4.88352, 4.90060, 4.93036, 4.92219,
     * 4.92669, 4.91829, 6.49922, 6.52887, 6.59625, 6.70134, 6.83170,
     * 6.92434, 6.89090, 6.84533, 6.76149, 8.77173, 8.82641, 8.93162,
     * 9.04918, 9.19211, 9.25937, 9.28937, 9.20574, 9.10944,10.21065,
     *10.26545,10.34529,10.46296,10.59320,10.74859,10.90421,11.02199,
     *11.01370
     * /
      DATA OGIV35 /
     * 4.64001, 4.64461, 4.68674, 4.79139, 4.83351, 4.77558, 4.95516,
     * 4.90951, 4.75120, 6.63578, 6.59047, 6.54482, 6.66187, 6.84167,
     * 6.70848, 6.66283, 6.64254, 6.57165, 8.65858, 8.70059, 8.76749,
     * 8.93468, 9.06434, 9.10635, 9.08605, 9.04041, 9.00727, 9.98984,
     *10.03196,10.04884,10.11575,10.37047,10.56289,10.64242,10.69693,
     *10.61388
     * /
C
      AMACH=MACH
      IF(MACH .LT. 0.8)AMACH=0.8
C
C***  DETERMINE NOSE TYPE
C
      IF(TNOSE .NE. 0.)GO TO 1000
C
C***  HERE FOR CONICAL NOSES
C
C***  INTERPOLATE AT EACH NOSE FINENESS RATIO
C
      CALL MVLOOK(1,3,5,15,0.0,TFA1,TMACH,CONE00,AMACH,FCENTR,0.0,CNA00)
      CALL MVLOOK(1,3,9,27,0.5,TFA1,TMACH,CONE05,AMACH,FCENTR,0.5,CNA05)
      CALL MVLOOK(1,3,6,18,1.5,TFA1,TMACH,CONE15,AMACH,FCENTR,1.5,CNA15)
      CALL MVLOOK(1,3,9,27,2.5,TFA1,TMACH,CONE25,AMACH,FCENTR,2.5,CNA25)
C
C***  NOW LINEARILY INTERPOLATE AT FNOSE
C
      XX(1)=0.0
      XX(2)=0.5
      XX(3)=1.5
      XX(4)=2.5
      XX(5)=10.
C
C ... REMOVE .4*FCENTR FROM DATA
C
      YY(1)=CNA00-.4*FCENTR
      YY(2)=CNA05-.4*FCENTR
      YY(3)=CNA15-.4*FCENTR
      YY(4)=CNA25-.4*FCENTR
      YY(5)=2.
      CALL LNTRP(XX,YY,5,FNOSE,CNA)
C
      GO TO 1010
 1000 CONTINUE
C
C***  OGIVAL (AND OTHER) SHAPES
C
C***  INTERPOLATE AT EACH NOSE FINENESS RATIO
C
      CALL MVLOOK(1,3,5,15,0.0,TFA1,TMACH,CONE00,AMACH,FCENTR,0.0,CNA00)
      CALL MVLOOK(1,4,9,36,0.5,TFA2,TMACH,OGIV05,AMACH,FCENTR,0.5,CNA05)
      CALL MVLOOK(1,4,9,36,1.5,TFA2,TMACH,OGIV15,AMACH,FCENTR,1.5,CNA15)
      CALL MVLOOK(1,4,9,36,2.5,TFA3,TMACH,OGIV25,AMACH,FCENTR,2.5,CNA25)
      CALL MVLOOK(1,4,9,36,3.5,TFA3,TMACH,OGIV35,AMACH,FCENTR,3.5,CNA35)
C
C***  NOW LINEARILY INTERPOLATE AT FNOSE
C
      XX(1)=0.0
      XX(2)=0.5
      XX(3)=1.5
      XX(4)=2.5
      XX(5)=3.5
      XX(6)=10.
C
C ... REMOVE .4*FCENTR FROM DATA
C
      YY(1)=CNA00-.4*FCENTR
      YY(2)=CNA05-.4*FCENTR
      YY(3)=CNA15-.4*FCENTR
      YY(4)=CNA25-.4*FCENTR
      YY(5)=CNA35-.4*FCENTR
      YY(6)=2.
      CALL LNTRP(XX,YY,6,FNOSE,CNA)
C
 1010 CONTINUE
C
      RETURN
      END
