C  MACH = 1.5 ARE LINES E & F,   MACH = 2.0 ARE LINES G & H 
C  MACH = 3.0 ARE LINES I & J,   MACH = 4.0 ARE LINES K & L 
C  MACH = 5.0 ARE LINES M & N 
C 
C  FOR EACH MACH NUMBER, THERE ARE 4 CURVES CORRESPONDING TO THE
C  4 INLET LENGTHS.  THE INLET LENGTHS ARE 1,2,3 AND 4. 
C  FOR EACH LENGTH, THERE ARE 5 POINTS CORRESPONDING TO THE CONE
C  HALF ANGLES.  THE HALF ANGLES ARE 6,10,14,18, AND 22 DEG 
C 
C  CXBLK TABLES 
      DATA CXB /
