      SUBROUTINE BDCAWF(DCENTR,DAFT,LAFT,MACH,CAWFL)
C
C*** THIS SUBROUTINE DETERMINES THE SUBSONIC-TRANSONIC WAVE DRAG
C*** COEFFICIENT OF AXISYMMETRIC CONICAL FLARES USING THE CURVES
C*** IN AMCP 706-280, FIGURE 8-33A-D, PAGE 8-49
C
C*** INPUTS
C
C***   DCENTR -- FOREWARD FLARE DIAMETER
C***     DAFT -- AFT FLARE DIAMETER
C***     LAFT -- LENGTH OF FLARE
C***     MACH -- FREE STREAM MACH NUMBER
C
C*** OUTPUT
C
C***    CAWFL -- WAVE DRAG COEFFICIENT INCREMENT TO AXIAL FORCE
C***             DUE TO FLARE (BASED ON AREA OF FORWARD DIAMETER)
C
      COMMON /CONST/ PI,RAD,UNUSED,KAND
C
      REAL MACH,LAFT
      DIMENSION TBMACH(5),THETAF(4),TABALL(120),DCODF(6)
      DIMENSION TAB050(24),TAB090(24),TAB100(24),TAB120(24),TAB130(24)
      EQUIVALENCE (TAB050(1),TABALL(1)),(TAB090(1),TABALL(25)),
     *            (TAB100(1),TABALL(49)),(TAB120(1),TABALL(73)),
     *            (TAB130(1),TABALL(97))
      DATA TBMACH /.5,.9,1.0,1.2,1.3/
      DATA THETAF /0.,5.,7.5,10./
      DATA DCODF /0.,.2,.4,.6,.8,1./
      DATA TAB050 /
     *  .0000, .0000, .0000, .0000, .0000, .0000,
     *  .0000, .0000, .0000, .0000, .0000, .0000,
     *  .0000, .0000, .0000, .0000, .0000, .0000,
     *  .0000, .0000, .0000, .0000, .0000, .0000/
      DATA TAB090/
     *  .0000, .0000, .0000, .0000, .0000, .0000,
     *  .0064, .0064, .0052, .0048, .0044, .0000,
     *  .0248, .0228, .0200, .0172, .0120, .0000,
     *  .0488, .0480, .0448, .0400, .0280, .0000/
      DATA TAB100/
     *  .0000, .0000, .0000, .0000, .0000, .0000,
     *  .0132, .0132, .0128, .0116, .0076, .0000,
     *  .0416, .0416, .0400, .0352, .0220, .0000,
     *  .0844, .0840, .0832, .0748, .0488, .0000/
      DATA TAB120/
     *  .0000, .0000, .0000, .0000, .0000, .0000,
     *  .0480, .0480, .0472, .0440, .0292, .0000,
     *  .0692, .0692, .0688, .0684, .0540, .0000,
     *  .1172, .1168, .1148, .1060, .0800, .0000/
      DATA TAB130/
     *  .0000, .0000, .0000, .0000, .0000, .0000,
     *  .0332, .0332, .0328, .0324, .0248, .0000,
     *  .0652, .0652, .0648, .0628, .0516, .0000,
     *  .1196, .1196, .1192, .1120, .0828, .0000/
C
      CAWFL=0.
C
C*** CHECK FOR VALID MACH NUMBER RANGE
C
      AMACH=MACH
      IF(MACH.LT.0.5) AMACH=0.5
      IF(MACH.GT.1.3) AMACH=1.3
C
      IF((LAFT.EQ.0.).OR.(DCENTR.EQ.0.)) GO TO 1000
      IF(DCENTR.GT.DAFT) GO TO 1000
C
C*** CALCULATE THETA
C
C
C  REV 8/99 CORRECT THETA CALCULATION
C
C     THETA = ATAN((DAFT-DCENTR)/LAFT)*RAD
      THETA = ATAN((DAFT-DCENTR)/(2.*LAFT))*RAD
C
C*** CALCULATE DIAMETER RATIO
C
      DIARAT = DCENTR/DAFT
C
C*** INTERPOLATE
C
      CALL MVLOOK(5,4,6,120,TBMACH,THETAF,DCODF,TABALL,
     *            DIARAT,THETA,AMACH,CAWFL)
C
 1000 CONTINUE
C
      RETURN
      END
