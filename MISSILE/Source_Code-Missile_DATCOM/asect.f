      SUBROUTINE ASECT
C
C***  COMPUTE AIRFOIL SECTION AERODYNAMICS
C
      COMMON /F1WORK/ F1WK(290)
      COMMON /F2WORK/ F2WK(290)
      COMMON /F3WORK/ F3WK(290)
      COMMON /F4WORK/ F4WK(290)
C
      COMMON /DESIG/  NACA(80,4)
C
      COMMON /FLC/    FLT(144)
C
      COMMON /GEOFS1/ GFIN1(188)
      COMMON /GEOFS2/ GFIN2(188)
      COMMON /GEOFS3/ GFIN3(188)
      COMMON /GEOFS4/ GFIN4(188)
C
      COMMON /FSET1/  FIN1(399)
      COMMON /FSET2/  FIN2(399)
      COMMON /FSET3/  FIN3(399)
      COMMON /FSET4/  FIN4(399)
C
      COMMON /LOGIC/ LDMPCS,LDAMP,LBUILD,LNACA,LDERDG,
     1               LDERRD,LPART,LNAME,LPLOT,
     2               LFLT,LREFQ,LAXIS,LFIN1,LFIN2,LFIN3,LFIN4,
     3               LDEFL,LTRIM,LDIMIN,LDIMFT,LDIMCM,LDIMM,LELLB,
     4               LINLET,LEXPR,LICRMT,LSPIN,LARBOD
      LOGICAL        LDMPCS,LDAMP,LBUILD,LNACA,LDERDG,
     1               LDERRD,LPART,LNAME,LPLOT,
     2               LFLT,LREFQ,LAXIS,LFIN1,LFIN2,LFIN3,LFIN4,
     3               LDEFL,LTRIM,LDIMIN,LDIMFT,LDIMCM,LDIMM,LELLB,
     4               LINLET,LEXPR,LICRMT,LSPIN,LARBOD
      COMMON /ASMVAR/TDATA(523)
      COMMON /CONST/ PI,RAD,UNUSED,KAND
C
      REAL MACH,NMACH
      CHARACTER*4 NACA
      DIMENSION MACH(20),REN(20)
C
      EQUIVALENCE (MACH(1),FLT(25)),(REN(1),FLT(65)),(NMACH,FLT(24))
C
      DO 1000 I=1,523
         TDATA(I)=UNUSED
 1000 CONTINUE
C
C***  FIN SET 1
C
      IF(LFIN1) CALL CLMAX(GFIN1(184),GFIN1(185),FIN1(113),FIN1(213),
     1          FIN1(163),NMACH,REN,F1WK(271))
      IF(LFIN1) CALL THEORY(NACA(1,1),NMACH,MACH,REN,1,F1WK(1),F1WK(2),
     1          GFIN1(185),GFIN1(184),FIN1(113),FIN1(213),FIN1(163),
     2          F1WK(4),F1WK(24),F1WK(44),F1WK(268),F1WK(269),
     3          F1WK(270),F1WK(271))
C
C***  FIN SET 2
C
      IF(LFIN2) CALL CLMAX(GFIN2(184),GFIN2(185),FIN2(113),FIN2(213),
     1          FIN2(163),NMACH,REN,F2WK(271))
      IF(LFIN2) CALL THEORY(NACA(1,2),NMACH,MACH,REN,2,F2WK(1),F2WK(2),
     1          GFIN2(185),GFIN2(184),FIN2(113),FIN2(213),FIN2(163),
     2          F2WK(4),F2WK(24),F2WK(44),F2WK(268),F2WK(269),
     3          F2WK(270),F2WK(271))
C
C***  FIN SET 3
C
      IF(LFIN3) CALL CLMAX(GFIN3(184),GFIN3(185),FIN3(113),FIN3(213),
     1          FIN3(163),NMACH,REN,F3WK(271))
      IF(LFIN3) CALL THEORY(NACA(1,3),NMACH,MACH,REN,3,F3WK(1),F3WK(2),
     1          GFIN3(185),GFIN3(184),FIN3(113),FIN3(213),FIN3(163),
     2          F3WK(4),F3WK(24),F3WK(44),F3WK(268),F3WK(269),
     3          F3WK(270),F3WK(271))
C
C***  FIN SET 4
C
      IF(LFIN4) CALL CLMAX(GFIN4(184),GFIN4(185),FIN4(113),FIN4(213),
     1          FIN4(163),NMACH,REN,F4WK(271))
      IF(LFIN4) CALL THEORY(NACA(1,4),NMACH,MACH,REN,4,F4WK(1),F4WK(2),
     1          GFIN4(185),GFIN4(184),FIN4(113),FIN4(213),FIN4(163),
     2          F4WK(4),F4WK(24),F4WK(44),F4WK(268),F4WK(269),
     3          F4WK(270),F4WK(271))
C
      RETURN
      END
