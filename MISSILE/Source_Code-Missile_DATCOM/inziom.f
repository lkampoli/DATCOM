      SUBROUTINE INZIOM
C
C***  INITIALIZE ALL IOM DATA ARRAYS AND INTERNAL WORK ARRAYS
C
C  MODIFIED BY W. BLAKE, WL/FIGC
C
C***  OUTPUT DATA
C
C***  STATICS ...
C
      COMMON /SBODY/ CBOD(220)
      COMMON /SFIN1/ CFIN1(220)
      COMMON /SFIN2/ CFIN2(220)
      COMMON /SFIN3/ CFIN3(220)
      COMMON /SFIN4/ CFIN4(220)
      COMMON /SB1/   CB1(220)
      COMMON /SB12/  CB12(220)
      COMMON /SB123/ CB123(220)
      COMMON /SB1234/CB1234(220)
C
C***  DYNAMICS ...
C
C
      COMMON /DBODY/ EBODY(406)
C     COMMON /DDFIN1/ EFIN1(80)
C     COMMON /DDFIN2/ EFIN2(80)
C     COMMON /DDFIN3/ EFIN3(80)
C     COMMON /DDFIN4/ EFIN4(80)
      COMMON /DB1/   EB1(400)
      COMMON /DB12/  EB12(400)
      COMMON /DB123/ EB123(400)
      COMMON /DB1234/EB1234(400)
C
C***  INTERNAL WORK ARRAYS
C
      COMMON /BDWORK/ BDW(241)
C
C***  PROGRAM CONSTANTS
C
      COMMON /CONST/  PI,RAD,UNUSED,KAND
C
      DO 1000 I=1,220
         CBOD(I)=UNUSED
         CFIN1(I)=UNUSED
         CFIN2(I)=UNUSED
         CFIN3(I)=UNUSED
         CFIN4(I)=UNUSED
         CB1(I)=UNUSED
         CB12(I)=UNUSED
         CB123(I)=UNUSED
         CB1234(I)=UNUSED
 1000 CONTINUE
C
C     DO 1005 I=1,80
C        EFIN1(I)=UNUSED
C        EFIN2(I)=UNUSED
C        EFIN3(I)=UNUSED
C        EFIN4(I)=UNUSED
C1005 CONTINUE
C
      DO 1010 I=1,400
         EBODY(I)=UNUSED
         EB1(I)=UNUSED
         EB12(I)=UNUSED
         EB123(I)=UNUSED
         EB1234(I)=UNUSED
 1010 CONTINUE
C
      DO 1015 I=1,46
         EBODY(360+I)=UNUSED
 1015 CONTINUE
C
      DO 1020 I=1,241
         BDW(I)=UNUSED
 1020 CONTINUE
C
      RETURN
      END
