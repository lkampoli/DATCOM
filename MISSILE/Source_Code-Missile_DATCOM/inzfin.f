       SUBROUTINE INZFIN(ICASE,SAVE)
C      
C***  INITIALIZE ALL FIN ARRAYS
C      
C***  FIN INPUTS
C      
      COMMON /FSET1/ SECTYP,SSPAN(10),LMAXU(10),LFLATU(10),LMAXL(10),
     1               LFLATL(10),CHORD(10),THICKU(10),THICKL(10),
     2               SWEEP(10),STA(10),XLE(10),NPANEL,
     3               XCORD(50),MEAN(50),THICK(50),YUPPER(50),
     4               YLOWER(50),FINPHI,LER(10),GAM(8),PHIF(8),
     5		     CFOC(10)
C      
      REAL LER,LMAXU,LMAXL,LFLATU,LFLATL,NPANEL,MEAN
C      
      DIMENSION FDATA1(399)
      EQUIVALENCE (FDATA1(1),SECTYP)
C      
      COMMON /FSET2/ FDATA2(399)
      COMMON /FSET3/ FDATA3(399)
      COMMON /FSET4/ FDATA4(399)
C      
C***  FIN GEOMETRIC CONSTANTS
C      
      COMMON /GEOFS1/ SPLANF(10),CBAR(10),SWEPLE(10),SWEP25(10),
     1                SWEP50(10),SWEP75(10),SWEPTE(10),
     2                XMGCLE(10),XMGC25(10),
     3                XMGC50(10),XMGC75(10),XMGCTE(10),YMEANC(10),
     4                ASPCT(10),TAPER(10),TOVERC(10),XCENT(10),
     5                FINAR,FINTPR,FINSPN,FINSP,FINWET,TCEFF,
     6                FINXGC,FINYGC,SWAVLE,SWAV25,SWAV50,SWAV75,
     7                SWAVTE,FINCBR,DELTAY,DELTAD,XCENF,ALER
C      
      DIMENSION GFIN1(188)
      EQUIVALENCE (GFIN1(1),SPLANF(1))
C      
      COMMON /GEOFS2/ GFIN2(188)
      COMMON /GEOFS3/ GFIN3(188)
      COMMON /GEOFS4/ GFIN4(188)
C      
C***  FIN WORK ARRAYS
C      
      COMMON /F1WORK/ F1WK(290)
      COMMON /F2WORK/ F2WK(290)
      COMMON /F3WORK/ F3WK(290)
      COMMON /F4WORK/ F4WK(290)
      COMMON /DESIG/  NACA(80,4)
C      
C***  PROGRAM CONSTANTS
C      
      COMMON /CONST/  PI,RAD,UNUSED,KAND
C      
      LOGICAL SAVE
C      
C***  INITIALIZE NACA IF PROGRAM GENERATED DESIGNATION
C      
      DO 1010 I=1,80
        IF(ICASE .EQ. 1)GO TO 1000
          IF(FDATA1(1) .NE. 1.) NACA(I,1)=UNUSED
          IF(FDATA2(1) .NE. 1.) NACA(I,2)=UNUSED
          IF(FDATA3(1) .NE. 1.) NACA(I,3)=UNUSED
          IF(FDATA4(1) .NE. 1.) NACA(I,4)=UNUSED
        GO TO 1010
 1000    NACA(I,1)=UNUSED
         NACA(I,2)=UNUSED
         NACA(I,3)=UNUSED
         NACA(I,4)=UNUSED
 1010 CONTINUE
      DO 1020 I=1,389
         FDATA1(I)=UNUSED
         FDATA2(I)=UNUSED
         FDATA3(I)=UNUSED
         FDATA4(I)=UNUSED
 1020 CONTINUE

C  SET DEFAULT VALUES FOR (FLAP CHORD/FIN CHORD)station i
C  (FLAP CHORD/FIN CHORD)station 1 DEFAULTS TO 1.
      FDATA1(390)=1.
      FDATA2(390)=1.
      FDATA3(390)=1.
      FDATA4(390)=1.
C  (FLAP CHORD/FIN CHORD)station i, where i=2,N, DEFAULTS TO 0.
      DO 1025 I=391,399
         FDATA1(I)=0.
         FDATA2(I)=0.
         FDATA3(I)=0.
         FDATA4(I)=0.
 1025 CONTINUE      

      DO 1030 I=1,188
         GFIN1(I)=UNUSED
         GFIN2(I)=UNUSED
         GFIN3(I)=UNUSED
         GFIN4(I)=UNUSED
 1030 CONTINUE
C      
      DO 1040 I=1,290
         F1WK(I)=UNUSED
         F2WK(I)=UNUSED
         F3WK(I)=UNUSED
         F4WK(I)=UNUSED
 1040 CONTINUE
C      
      RETURN
      END
