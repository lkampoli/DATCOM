      FUNCTION ELLIP2(CAYSQ)
C
C***  COMPUTE THE COMPLETE ELLIPTIC INTEGRAL OF THE SECOND
C***  KIND USING A POLYNOMIAL EXPANSION
C
C***  INPUT
C
C   CAYSQ - VALUE FOR K SQUARED (MULTIPLIER TO SIN(PHI)**2)
C
C         FUNCTION INTEGRATED=SQRT(1.-CAYSQ*SIN(PHI)**2)
C              OVER THE RANGE OF PHI FROM 0 TO PI/2
C
C***  REFERENCE - HANDBOOK OF MATH FUNCTIONS
C                 BY ABRAMOVITZ AND STEGUN
C                 PAGE 592, EQUATION 17.3.36
C
      ELLIP2=1.
C
      IF(CAYSQ .GE. 1.)GO TO 1000
C
      CAY=1.-CAYSQ
C
      ELLIP2=1.           +.44325141463*CAY+.06260601220*CAY**2+
     1                     .04757383546*CAY**3+.01736506451*CAY**4+
     2       ALOG(1./CAY)*(   .24998368310*CAY+.09200180037*CAY**2+
     3                     .04069697526*CAY**3+.00526449639*CAY**4)
C
 1000 CONTINUE
C
      RETURN
      END
